        <h2>Sanayide Kadın İstihdamına Destek</h2>
        
    <p>

        Borusan, Aile ve Sosyal Politikalar Bakanlığı ile Bilim, Sanayi ve Teknoloji Bakanlığı iş birliğiyle sanayide kadın istihdamının artırılması ve kadın istihdamının artırılması amacıyla “Annemin İşi Benim Geleceğim” adlı kurumsal sosyal sorumluluk projesini hayata geçirdi.
        <br />
        <br />
Bu kapsamda Borusan, Türkiye genelinde 10 farklı ilde 10 Organize Sanayi Bölgesi'nde (OSB) 0-6 yaş arası çocuklara yönelik "Borusan Neşe Fabrikası" adı altında kreşler açıyor . Borusan Neşe Fabrikaları'nın amacı, çocuklarına bakmaktan dolayı iş bulamayan kadınların hayatlarını kolaylaştırmak ve bu bağlamda vasıflı kadın işgücünün Türkiye ekonomisine katkısını artırmaktır.
         <br />
        <br />
“Annemin İşi Benim Geleceğim” projesi, kadınların genel olarak güçlenmesinin yanı sıra, daha fazla kadının ekonomiye katılımını kolaylaştırarak ailelerin ve milletimizin ekonomik durumunun güçlendirilmesinin de önünü açıyor. Sanayi sektörünün işgücü piyasası.
         <br />
        <br />
0-6 yaş arası çocukların bakımı ve eğitimi için kurulacak Borusan Neşe Fabrikaları'nın hedefleri arasında şunlar yer alıyor: •
Çocuk bakımı olmadığı için normalde çalışamayacak durumda olan kadınlara fırsat sağlamak;
• Kadınların işgücüne katılımının önündeki engellerin kaldırılması; Sanayi sektöründe kadın çalışan oranının artırılması;
• Normalde okul öncesi bakım ve eğitim hizmetlerine erişemeyecek olan çocukların zihinsel ve bedensel gelişimlerine uygun eğitim ortamının oluşturulması.
         <br />
        <br />
"Annemin İşi Benim Geleceğim" projesinin desteğiyle özellikle Anadolu Bölgesi'nde kadın istihdam oranının artması bekleniyor. Borusan Neşe Fabrikası'nda kadınlar, çocuklarının mutlu, güvenli ve sağlıklı bir ortamda olduğunu bilerek işlerine tam bir huzur ve mutlulukla gidebilecek.
         <br />
        <br />
İlk Borusan Neşe Fabrikası Adıyaman ve Afyonkarahisar'da açıldı. Ayrıca Malatya ve Balıkesir'de bulunan kreşlerin inşaatı da tamamlandı.
         <br />
        <br />
Borusan Neşe Fabrikaları'nın mimari tasarımı, Türkiye'nin dört bir yanından mimarların katıldığı, jürisinin akademisyenler ve tanınmış mimarlardan oluştuğu ulusal mimari proje yarışmasıyla belirlendi.
         <br />
        <br />
Proje hakkında detaylı bilgi için: <a href=" www.anneminisi.org">www.anneminisi.org </a> adresini ziyaret edebilirsiniz.
