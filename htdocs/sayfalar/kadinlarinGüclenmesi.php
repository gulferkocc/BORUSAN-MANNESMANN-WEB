    <h2>Güçlü Kadın, Güçlü Türkiye</h2>
      <p>Borusan, kadınların iş hayatına katılımının Türkiye'deki gelişim sürecinin ayrılmaz bir parçası olduğu inancıyla, Türkiye'nin en köklü sanayi kuruluşlarından biri olarak çözümün parçası olmaya çalışıyor.
        <br />
        <br />
Borusan Holding, 2013 yılında Birleşmiş Milletler Kadının Güçlenmesi İlkeleri'ne imza atarak, kadının iş hayatında ve toplumdaki konumunu güçlendirmeye kararlıdır. Birleşmiş Milletler Cinsiyet Eşitliği ve Kadının Güçlenmesi Birimi'nin (UNWomen) ortak girişimi kapsamında ve Birleşmiş Milletler Küresel İlkeler Sözleşmesi (UNGC) uyarınca Kadının Güçlenmesi İlkeleri, işyerlerinde, pazarlarda ve toplum genelinde toplumsal cinsiyet eşitliğinin sağlanması için özel sektörün dikkate alması gereken önemli unsurları ortaya koymaktadır.
         <br />
        <br />
Ayrıca Borusan Holding, 2012 yılında kadınların Türkiye ekonomisine katılımı sürecinde cinsiyet eşitliğini sağlamak amacıyla kurulan, kamu ve özel sektör işbirliklerini de içeren çok paydaşlı bir yapıya sahip olan 'İş'te Eşitlik Platformu'na katıldı. STK'lar, akademisyenler, medya ve Dünya Ekonomik Forumu.
         <br />
        <br />
Borusan, Türkiye'nin en temel konularından biri olan toplumsal eşitlik konusunda sosyal sorumluluk projelerine imza atmaya devam ediyor.</p>
