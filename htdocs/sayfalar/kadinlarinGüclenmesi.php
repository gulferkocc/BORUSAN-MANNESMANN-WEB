        <h2>Kadın Girişimcileri Destekliyoruz</h2>

        <p>
            Türkiye Grameen Mikrofinans Programının (TGMP) 50. şubesi olarak kurulan Afyonkarahisar Asım Kocabıyık Mikrofinans Şubesi, Borusan Kocabıyık Vakfı, Afyonkarahisar Valiliği ve Türkiye İsrafı Önleme Vakfı (TİSVA) arasında imzalanan protokol kapsamında 2009 yılında hizmete açıldı. .
              <br />
        <br />
Nisan 2013'te Gemlik Borusan Mikrofinans Şubesi ayrıca Bursa Gemlik'te hizmete açıldı. TGMP'nin 95'inci şubesi olarak faaliyete geçen Gemlik Borusan Mikrofinans Şubesi, etki alanını genişletmezken, ilçenin merkez ve köylerinde bilgilendirme toplantıları düzenleyerek mikrokredinin amaç ve zihniyetini tüm ihtiyaç sahibi kadınlara aktarma konusunda hızlı bir ilerleme kaydetti. Gemlik.
              <br />
        <br />
Borusan, iki mikrofinans şubesi sayesinde fırınlardan güzellik salonlarına, gıdadan el sanatlarına kadar birçok iş kolunda faaliyet gösteren bin 3500'ü aşkın kadına toplam 8 milyon TL tutarında kredi sağladı.






