    <h1>Borusan EnBW Enerji</h1>
   <p style="color:orangered">
       Portföyünün tamamı yenilenebilir enerji kaynaklarına dayalı olan Borusan EnBW Enerji'nin toplam kurulu gücü 2021 yılında 725 MW'a yükseldi.
</p>
   <br />
    <p>
        2007 yılında enerji sektörüne adım atan Borusan, 2009 yılında Almanya'nın üçüncü büyük enerji şirketi EnBW AG ile ortak girişime girmiştir.
        <br />
        <br />
Borusan EnBW Enerji, enerji sektöründeki yatırımları ve portföyüyle Türkiye için değer yaratmaya odaklanmıştır. Portföyünün tamamı yenilenebilir enerji kaynaklarına dayanan Borusan EnBW Enerji, ilk yatırımı olarak Bandırma Rüzgar Enerjisi Santrali'ni (WEPP) 2009 yılında devreye aldı. 2014 yılında gerçekleştirilen kapasite artışı sonucunda santralin kurulu gücü 91,4 MW'a çıkarıldı.
          <br />
        <br />
Borusan EnBW Enerji'nin ilk hidroelektrik santrali olan Erzurum'da bulunan 50,3 MW Yedigöl Aksu Hidroelektrik Santrali ilkiyle devreye alındı. Aşama 2011'de başladı ve ardından 2012'de ikinci aşama geldi.
          <br />
        <br />
İşletmeye alınan Borusan EnBW Enerji'nin toplam kurulu gücü, </p> <br />
        
<li>2014 yılında Balabanlı RES (Tekirdağ - 50,6 MW; 2017 kapasite artırımı 61,4 MW);</li>
<li>Harmanlık RES (Bursa,- 52,8 MW), Koru RES (Çanakkale - 52,8 MW), Mut RES (Mersin – 52,8 MW), Fuat RES (İzmir - 33 MW) 2015 yılında;</li>
<li>Dayıcık Güneş Enerjisi Santrali (GES) (Mersin - 6,6 MW) 2016 yılında;</li>
<li>Kartaldağı RES (Gaziantep - 65,55 MW) ve Pamuklu GES (Mersin - 2,2 MW) ile Kıyıköy RES (Kırklareli, 28 MW)'nin portföye eklenmesi.   </li>
    <p>
2017 yılında Kıyıköy RES'i (Kırklareli - 28 MW) portföyüne ekleyen Borusan EnBW Enerji, kapasite artırımıyla santralin kurulu gücünü 2021 yılında 100 MW'a çıkardı. 2021 yılında Saros RES'i devreye almaya başlayan (143 MW / tamamlandığında 148,5 MW'a ulaşacak) Borusan EnBW Enerji, 12 yenilenebilir enerji tesisiyle toplam 720 MW kurulu güce sahiptir.
        <br />
        <br />
Borusan EnBW Enerji, 1200 MW civarındaki portföyüyle Türkiye'nin lider rüzgar enerjisi üretim şirketi olmayı hedefliyor.
    </p>
