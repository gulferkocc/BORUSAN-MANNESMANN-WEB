<h1>DÜNYAMIZ İÇİN</h1>
<p>Borusan Mannesmann; operasyonlarının her aşamasında, çevreye olan olumsuz etkileri en aza indirmek için stratejiler belirliyor; temiz bir çevre ve sağlıklı bir gelecek için çevre dostu politikalar izliyor. Üretim aşamasındaki atıkları geri kazandırmak, doğal kaynak kullanımını en aza indirmek, gelecek nesillere sağlıklı bir çevre sunmak için, tüm ulusal ve uluslararası yasal düzenlemelere uygun hareket ediyor.
    <br />
    <br />
 

Borusan Mannesmann’ın sürdürülebilirlik çalışmaları Borusan Grubu çatısı altında düzenli olarak raporlanıyor. Bu kapsamda, küresel düzeyde geçerliliği olan GRI Sürdürülebilirlik Rehberi’nin temel alınmasıyla hazırlanan ilk Borusan Sürdürülebilirlik Raporu 2009’da yayınlandı. Aralık ayında ise Birleşmiş Milletler’in New York’taki Global Compact Merkezi tarafından “Notable”, yani BM standartları açısından istisnai ölçüde başarılı ve yeterli bulundu.

 <br />
    <br />
 

Aynı zamanda Borusan Mannesmann, kurumsal ve bireysel bilgi, beceri ve birikimlerimizi toplumla paylaşmamıza katkıda bulunan Borusan Okyanus Gönüllüler Platformu ile birlikte çevreye dost projeler üretiyor ve çeşitli STK’lar ile ortak projelerde yer alıyor.</p>

    <h3>Enerji Politikamız</h3>
 <p>Enerji Politikamız ile;</p>
     <br />
<li>Enerji kullanımı, tüketimi ve verimliliği ile ilgili ulusal ve uluslararası yasal mevzuat ve diğer şartlara uymayı,</li>
<li>Amaç ve hedeflerimiz doğrultusunda sürekli iyileşmeyi hedefleyen bir enerji yönetim sistemi için gerekli tüm bilgi ve kaynakları temin ederek faaliyetlerimizi gerçekleştirmeyi,</li>
<li>Üretimlerde ve tüm süreçlerimizde yeni teknolojileri izlemeyi,</li>
<li>Enerji verimli ürünler ve hizmetler satın alıp enerji performansının iyileştirilmesine katkıda bulunan tasarımları tercih etmeyi,</li>
<li>Enerjiyi ve doğal kaynakları verimli kullanarak enerji kayıplarını azaltmayı,</li>
<li> Tüm paydaşlar ile etkin bir iletişim kurarak toplumun ve çalışanlarımızın ve enerji verimliliği konusunda bilincini artırmayı,</li>
<li> Ölçülebilen performans göstergeleri oluşturarak gelişimimizi düzenli denetimler ve raporlarla izlemeyi, taahhüt ederiz.</li>


