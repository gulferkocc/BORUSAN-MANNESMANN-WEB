   <h1>Borusan Çağdaş</h1>
    <br />
<p>Borusan Contemporary Perili Köşk İstanbul, Borusan Çağdaş Sanat Koleksiyonu'nu daha geniş kitlelere tanıtmak ve Türkiye'de çağdaş sanata olan ilgiyi güçlendirmek vizyonuyla kuruldu. "Ofis-müze" konseptini temel alan Borusan Contemporary, eserlere ev sahipliği yapan benzersiz fiziksel ortamı ve farklı bir ziyaret deneyimiyle medya sanatı alanında öncü olmayı hedefliyor.
    <br />
    <br />
Borusan Çağdaş Sanat Koleksiyonu'nun öne çıkan eserleri Perili Köşk'ün ofis katlarında uzun süre sergileniyor ve her hafta sonu sanatseverler tarafından ziyaret edilebiliyor. Hafta içi Borusan Holding'in ofisi olarak hizmet veren bina, hafta sonları ise ulusal ve uluslararası tanınmış küratörlerin düzenlediği koleksiyon, kişisel ve karma sergilerden çeşitli seçkilere ev sahipliği yapıyor.
    <br />
    <br />
Müzenin bir diğer hedefi de sergileri her yaş grubundan çocuklara ve yetişkinlere yönelik eğitim programları ve akademik tartışmalarla destekleyerek bilgi üretimine katkıda bulunmak ve bu sayede Türkiye'nin sanatsal ve kültürel ortamını doğrudan etkilemektir.
    <br />
    <br />
Borusan Contemporary, sanat mağazası ve kafeteryasıyla Türkiye'de eşi benzeri olmayan, gelişmiş ve benzersiz bir müze deneyimi sunuyor.
