    <p>
        Ülkemizde ve dünyadaki birçok önemli proje ve yapının mekanik, sıhhi tesisat ve yangın güvenlik sistemlerinde Borusan Mannesmann markalı borular tercih ediliyor. 65 yılı aşan uzmanlığımızla ürettiğimiz, uluslararası kalite belgelerine sahip ve standartlara uygun üretilen siyah, galvanizli, yivli mekanik tesisat boruları ve sunduğu hizmet kalitesiyle Borusan Mannesmann güvencesi her zaman sizinle...

 <br />
        <br />

Bir mekanik tesisatın hayati enstrümanlarından biri olan çelik su borularında korozyondan kaçınmak sağlıklı bir yapının vazgeçilmezlerindendir. Korozyon korumasının yanı sıra Borusan Mannesmann galvanizli su boruları düşük kurşun oranlı çinko kullanımı sayesinde, insan sağlığı için tehdit oluşturmaz. Sektörde siyah boru, galvanizli boru, galvanizli dişli manşonlu boru, testli boru olarak da bilinen su tesisat borumuz NSF (National Sanitation Foundation) ve DVGW (Deutscher Verein des Gas- und Wasserfaches) belgelidir. Kuzey Amerika ve Almanya’da aldığı bur sertifikalar sayesinde içme suyu tesisatında kullanılabilir kalitede ve güvendedir.

  <br />
        <br />

Borusan Mannesmann SRM tesisat boruları ise üstün özellikleri ile bu kullanım alanında birçok farklı ürünün önüne geçiyor. Zamandan tasarruf sağlayan ve kolay şekil alan SRM boruları uzun ömürlü olması ve kireç tutmayan iç yüzeyi ile özellikle ısıtma sistemlerinde tercih ediliyor.
         <br />
        <br />
 

Bu alanda öne çıkan bir başka ürün; Türkiye’nin ve Avrupa’nın EN ve ASTM standardında UL & FM onaylı ilk ve tek borusu olan Borusan Mannesmann Firesist™ Yangın Güvenlik ve Tesisat Boruları, korozyon korunumu sağlayan yapısı ile yatırımcı ve yüklenici firmalara düşük ek işlem maliyeti, zaman, işçilik ve fire tasarrufu sağlar. Borusan Mannesmann Firesist™ boruları stok üretimi ile anında teslim edilebilmektedir.
