    <h1>Borusan Otomotiv Grubu</h1>
    <p>
        Borusan Otomotiv Grubu premium otomobil segmentinde altı şirketiyle faaliyet gösteriyor: Borusan Otomotiv İthalat, Borusan Otomotiv Pazarlama, Borusan Oto, Borusan Oto Kıbrıs, Borusan Otomotiv Premium ve BOM Motor Sports.
        <br />
        <br />
Borusan Holding ile Almanya'da faaliyet gösteren GIWA Holding arasında iş ortaklığı olarak kurulan şirket, Borusan Otomotiv Grubu 1984 yılında 47 kişilik bir ekiple BMW otomobillerinin Türkiye distribütörlüğünü devraldı. Şirket, marka yelpazesini daha da genişletti. 1992 yılında motosiklet tutkunlarının favori markası BMW Motorrad, ardından 1998 yılında en iyi 4x4 araçların üreticisi Land Rover. Bunu 1999 yılında küçük otomobil segmentinde fark yaratan MINI'nin eklenmesi takip etti ve son olarak 2014 yılında Jaguar markasıyla tamamlandı. lüks, konfor ve performansın birleşimini temsil ediyor.
        <br />
        <br />
Borusan Otomotiv İthalat, 35 yılı aşkın süredir BMW, MINI ve BMW Motorrad markalarının Türkiye distribütörlüğünü yaparken, Borusan Otomotiv Pazarlama ise Jaguar ve Land Rover markalarının distribütörlüğünü yürütmektedir.
        <br />
        <br />
Borusan Otomotiv Grubu, Türkiye'de 7 coğrafi bölgede 22 Yetkili Satıcı ve 49 Yetkili Servis aracılığıyla satış ve satış sonrası hizmetler sunmaktadır. Borusan Oto, Borusan Otomotiv'in temsil ettiği tüm markaların satış ve satış sonrası hizmetlerini yürütmekte olup 11 tesiste Borusan Otomotiv Yetkili Satıcısı ve Yetkili Servisi olarak faaliyet göstermektedir; Jaguar ve Land Rover Yetkili Satıcısı ve Yetkili Servisi olarak faaliyet gösteren Borusan Oto Kıbrıs, Kuzey Kıbrıs tesisinde müşterilerine mümkün olan en iyi deneyimi yaşatmaya odaklanarak hizmet veriyor.
        <br />
        <br />
Borusan Otomotiv Premium, Borusan Otomotiv Grubu'nun bir iştiraki olarak faaliyet göstermekte olup, Borusan Otomotiv'in distribütörlüğünü yaptığı BMW, MINI, Jaguar ve Land Rover için tüm bayilikleri üzerinden uzun vadeli kiralama hizmetleri, finansman, sigorta çözümleri ve ikinci el araç operasyonları sunmaktadır. yetkili bayilikler.
        <br />
        <br />
Ayrıca Borusan Otomotiv, Türkiye'de motor sporlarının gelişimine ve Türkiye'nin yurt dışında tanıtımına katkıda bulunmak amacıyla 2008 yılında Borusan Otomotiv Motorsport'u (BOM) kurdu. O zamandan beri takım 400'e yakın yarışa katıldı ve dört kıtada, 20 ülkede, 37 farklı pistte 18 şampiyonluk kazandı. Ayrıca BOM Akademi, Türkiye Otomobil Sporları Federasyonu ve Gençlik ve Spor Genel Müdürlüğü tarafından lisanslı bir eğitim kurumu olan Borusan Otomotiv Motorsport bünyesinde 2015 yılında kuruldu. BOM ayrıca gelişen e-spor sektöründe yer almak ve Türkiye'yi temsil etmek amacıyla 2018 yılında BOM E-Team'i kurarak faaliyetlerini genişletti. BOM Karting, genç sporcuları motor sporlarına kazandırmak ve bu sporun altyapısının geliştirilmesine katkıda bulunmak amacıyla 2020 yılında kurulmuştur.
        <br />
        <br />
Borusan Otomotiv Grubu, otomotiv sektöründeki yenilikçi hizmetleriyle müşterilerine benzersiz bir deneyim, paydaşlarına ise sürdürülebilir bir değer sunuyor. Şirket vizyonunu premium otomobil ve motosiklet segmentinde ilk tercih edilen ve öncü şirket olmak olarak tanımlıyor. Grup, stratejik hedefleri doğrultusunda katma değerli iş süreçleri oluşturmak için güçlü bir sinerji yaratmaktadır.
        <br />
        <br />
Borusan Otomotiv Grubu, sürdürülebilirlik yol haritasını topluma, çevreye ve müşteriye fayda ilkesi doğrultusunda “Türkiye Otomotiv Sektörünün Elektrifikasyon Dönüşümünde Öncü Olmak” vizyonuyla tanımladı.

