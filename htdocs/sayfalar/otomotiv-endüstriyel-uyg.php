    <p>Borusan Mannesmann olarak 65 yılı aşan uzmanlığımız ve kalite anlayışımızla, endüstriyel uygulamalar alanında otomotiv boruları, hidrolik silindir boruları, sanayi boruları ve profilleri üretiyoruz. Stratejik büyüme alanlarımızdan biri olan otomotiv segmenti için Türkiye’de Halkalı’daki uzmanlaşmış tesisimiz, Gemlik’teki son teknoloji tesisimiz ve Bursa’daki ileri işlem servis merkezimizle, İtalya’da ise Vobarno’daki tesisimizle araçların önemli parçalarında kullanılan katma değerli hassas boruları üretiyoruz. Bir aracın amortisöründen hidrolik direksiyon borusuna, kardan milinden egzozuna, koltuk iskeletinden şaftına kadar 20 farklı nokta için özel üretim yapıyor, ileri işlemlerle de müşterilerimizin bazı süreçlerini elimine etmelerine yardımcı oluyoruz

 <br />
<br />

Özellikle bu ürün grubunda müşterimize değer yaratmak için 4 ana konuya odaklanıyoruz: 

 <br />
        <br />

<li> Tedarik zinciri yönetimi ile müşterilerimizin talep ettikleri ürüne en hızlı şekilde ulaşabilmeleri için hammadde ve üretim planlamamızı titizlikle yapıyoruz.</li>

<li> Müşterimizin işini büyütebilmesi, yeni fırsatlar yakalayabilmesi için çaba gösteriyor, AR-GE departmanlarıyla beraber çalışarak projelerimizi geliştiriyoruz.</li>

 <li>Müşterimizi yaşadığı problem ne olursa olsun çözümsüz bırakmıyor, zaman geçirmeden gerekli önlemleri alıyoruz.</li>

<li> Müşterilerimizin çıkarlarını gözetiyor, uzmanlığımız ve sürekli verdiğimiz teknik destekle süreçlerini onların faydasına iyileştiriyoruz.</li>

 <br /><br />

Otomotiv sektörünün yanı sıra geniş üretim yelpazemizle, hidrolik silindir borusundan mobilya ve beyaz eşya parçalarına, pusetten bisiklete birçok farklı alanda hizmet vermeye devam ediyoruz.
        <br />
        <br />
 

Borusan Mannesmann otomotiv boruları kalitesi ile otomotiv sektöründe oldukça bilinen ve sıkça kullanılan borulardır. Vobarno-İtalya, Gemlik-Türkiye ve Halkalı-Türkiye fabrikalarımız otomobil, hafif ve ağır ticari araçlarda kullanılan otomotiv borularının katma değerli üretimi konusunda uzmanlaşmıştır. Satış, kalite ve tasarım ekiplerimiz birlikte çalışarak her türlü teknik ve şematik sorgulamayı yapar ve müşterilerimize özel çözümler üretir.
        <br />
        <br />
Borusan Mannesmann hidrolik silindir boruları hafriyat makineleri, tarım makineleri ve kamyonlar gibi çeşitli araçlarda kullanılır. Borusan Mannesmann, honlama ve silindir parlatma gibi alanlar için kullanıma hazır hidrolik silindir borusu üretme kabiliyeti ile hidrolik uygulamalar için ideal bir tedarikçidir.
        <br />
        <br />
 

Özellikle mobilya ve beyaz eşya üretiminde kullanılan sanayi boru ve profilleri alanında Borusan Mannesmann; uzmanlığı sektör tarafından kabul edilmiş bir üreticidir. Buzdolabı, çamaşır makinesi, bulaşık makinesi vs. gibi beyaz eşyalar ve mobilyaların içerisinde yer alan sanayi boru ve profilleri müşteri talebi doğrultusunda minimum toleranslar ile üretilip teslim edilir.

 <br /><br />

Müşteri beklentilerine göre fonksiyonelliği yüksek özel şekilli profiller üreterek; otomotiv, inşaat, tarım makineleri, havlu rayları, mobilya vb. gibi farklı sektörlere hizmet veriyoruz.</p>

