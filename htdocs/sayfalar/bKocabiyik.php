   <h2> Eğitime Destek, Gelecek Nesillere Katkı</h2>
<p>Asım Kocabıyık'ın eğitime destek vizyonunu gerçekleştirerek Kocabıyık ailesi ve Borusan Grup Şirketlerinin bağışlarıyla kurulan Borusan Kocabıyık Vakfı, 1992 yılından bu yana eğitim alanında topluma değer katıyor. Milli eğitim sistemine temel oluşturmak bugüne kadar verdiği hizmetlerin en önemli unsurlarıdır:
      <br />
        <br />
<li>Borusan Asım Kocabıyık Anadolu Meslek Lisesi (Beylikdüzü, İstanbul)</li>
<li>Borusan Otomotiv Zehra Nurhan Kocabıyık İlköğretim Okulu (Avcılar, İstanbul)</li>
<li> Asım ve Nurhan Kocabıyık Öğretmenevi (Avcılar, İstanbul)</li>
 <li>Asım Kocabıyık Teknik Meslek Yüksekokulu (Kocaeli Üniversitesi)</li>
 <li>Asım Kocabıyık Teknik Meslek Yüksekokulu (Uludağ Üniversitesi)</li>
<li>İki öğrenci yurdu (Kocaeli Üniversitesi)</li>
<li> Hukuk Fakültesi Enstitüsü ve Dekanlık (Uludağ Üniversitesi)</li>
<li> Hukuk Fakültesi (Afyon Kocatepe Üniversitesi)</li>
<li> Öğrenci Merkezi (Uludağ Üniversitesi Gemlik Sunğipek Asım) Kocabıyık Yerleşkesi)</li>
      <br />
        <br />
<p>Ayrıca Borusan Kocabıyık Vakfı, okul öncesi eğitim çağındaki çocuklara temel etik değerlerin aşılanabilmesi amacıyla anaokulu öğretmenlerine destek verdi. Bu doğrultuda Vakıf, okul öncesi eğitim uzmanı Prof. Dr. Gülçin Alpöge'nin ebeveynlere ve öğretmenlere yol gösterici olması amacıyla yazdığı "Okul Öncesi Değerler Eğitimi" adlı kitabın sponsorluğunu üstlendi. Kitap, İstanbul İl Milli Eğitim Müdürlüğü işbirliğiyle İstanbul'daki 5 bini aşkın anaokulu öğretmenine ulaştırıldı.
      <br />
        <br />
Gemlik halkına katkı sağlayan yatırımlarıyla öne çıkan Borusan, 2012-2013 eğitim öğretim yılı itibarıyla hizmete açılan Umurbey Anadolu Sağlık Meslek Lisesi'ne kütüphane yaptırdı. Eğitime verdiği önem ve güncel hizmetleriyle tanınan Borusan Holding Kurucusu ve Onursal Başkanı Asım Kocabıyık'a 88. yaş gününde Borusan Grup Şirketleri tarafından hediye edilen kütüphanede 3 bin 500 kitap ve kaynak kapasitesi bulunuyor. . Kütüphanede öğrenciler, dizüstü ve masaüstü bilgisayarların yardımıyla çalışmaları ile ilgili her türlü bilgiye ulaşabilmekte ve tıp dünyasındaki son gelişmeleri yakından takip edebilmektedirler.
      <br />
        <br />
KalDer (Türkiye Kalite Derneği) tarafından gerçekleştirilen Ulusal Kalite Hareketi çalıştayları Avcılar İlçe Milli Eğitim Müdürlüğü, Borusan Otomotiv Zehra Nurhan Kocabıyık İlköğretim Okulu, Borusan Asım Kocabıyık Anadolu Meslek Lisesi, Asım Kocabıyık Teknik Meslek Yüksekokulu olmak üzere 6 eğitim biriminde devam ediyor. Kocaeli Üniversitesi Asım Kocabıyık Teknik Meslek Yüksekokulu ve Gemlik Borusan İlköğretim Okulu.
      <br />
        <br />
<b>Avcılar Pilot Projesi</b>
    <br />
    <br />
Borusan Kocabıyık Vakfı, velileri bilinçlendirerek eğitim kalitesini artırmak amacıyla 2007 yılında Avcılar İlçe Milli Eğitim Müdürlüğü ile birlikte bir proje başlattı. Vakıf, ebeveynlere yönelik tasarladığı proje kapsamında "Çocuk ve Gençlerde Şiddet, Saldırganlık ve Suçluluk Duyguları Açısından EBEVEYNLERE YÖNELİK EĞİTİM" başlıklı 30 bin kitabın yayınlanmasını sağladı. Proje kapsamında Avcılar İlçe Milli Eğitim Müdürlüğü koordinasyonunda bugüne kadar 18 bin veliye seminerler veriliyor.
      <br />
        <br />
Bu projeye paralel olarak yürütülen öğretmenlere yönelik "Hizmet İçi Çalışma Alanı İyileştirme Faaliyetleri" kapsamında bugüne kadar 7 ayrı üniversiteden 9 öğretim üyesinin katılımıyla toplam 144 saat eğitim verilmektedir. Gönüllü katılım esasıyla gerçekleştirilen bu eğitimlere Avcılar bölgesindeki öğretmenlerin büyük çoğunluğu katıldı.
</p>
   <h1> Burslar</h1>
<p>Borusan Kocabıyık Vakfı, kurulduğu ilk günden bu yana eğitime eşi benzeri olmayan fonlar üretiyor. Vakıf, maddi imkanları kısıtlı olan başarılı öğrencilere burs imkanı sunmaktadır.
    <br />
        <br />
Program kapsamında şu anda 150 gelecek vaat eden öğrenci Vakfın verdiği burslardan yararlanıyor.
    <br />
        <br />
Çağdaş Yaşamı Destekleme Derneği işbirliğiyle "Baba Beni Okula Gönder" projesi kapsamında her yıl 100 kız çocuğunun eğitimine destek veriliyor.
