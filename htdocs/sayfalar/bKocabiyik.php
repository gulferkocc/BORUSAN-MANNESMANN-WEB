    <h1>Borusan Kocabıyık Vakfı</h1>
    <h2>Türkiye için Değer Yaratıyoruz!</h2>
    <br />
    <p>
        Türkiye'nin önde gelen sanayi kuruluşlarından biri olan Borusan, toplumdan kazandığını yine toplumla paylaşma ilkesini benimseyerek ilerleme kaydetti. Kurumsal sosyal sorumluluk projeleri kapsamında eğitim, kültür ve sanata odaklanan Borusan, bir yandan milli değer yaratarak büyümesini sürdürürken, diğer yandan kamu kaynaklarının yetersiz kaldığı her alanda topluma katkı sağlamayı görev edindi. .
        <br />
        <br />
Gerek Borusan'ın kaynakları gerekse kişisel girişimleriyle uzun yıllar ulusal eğitim ve kültür hayatına katkıda bulunan Borusan'ın Kurucu ve Onursal Başkanı Kocabıyık, bu yolda atılan ilk adım olarak 1992 yılında Asım Kocabıyık Kültür ve Eğitim Vakfı'nı kurdu. bu faaliyetleri kurumsal bir yapı çatısı altında yürütmek.
        <br />
        <br />
"Topluma Katkı" vizyonuyla hareket ederek, sosyal sorumluluk çalışmaları kapsamında 1997 yılında kurulan Asım Kocabıyık Kültür ve Eğitim Vakfı ile Borusan Sanat'ı 2008 yılında Borusan Kocabıyık Vakfı çatısı altında buluşturdu.
        <br />
        <br />
<a href=" http://www.bkv.org.tr">http://www.bkv.org.tr</a>
    </p>
