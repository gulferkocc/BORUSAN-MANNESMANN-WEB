    <h1>Lojistik Grubu</h1>
    <p style="color:orangered">Borusan Lojistik, “yüksek teknolojik altyapısı, müşteriye özel çözümleri ve her alanda lider olma vizyonuyla” yoluna devam ediyor</p>
    <p>
        
1973 yılında kurulan Borusan Lojistik, 2000 yılından bu yana “entegre lojistik hizmet sağlayıcısı” olarak Türkiye Lojistik Hizmetleri, Liman Hizmetleri ve Chartering Proje Taşımacılığı, Uluslararası Taşımacılık Hizmetleri, Yurtdışı Lojistik Hizmetleri olmak üzere dört stratejik alanda hizmet vermektedir.
        <br />
        <br />
Borusan Lojistik, Türkiye Lojistik Hizmetleri kapsamında depolama, gümrükleme, yurt içi kara taşımacılığı, özel dağıtım, proje ve ağır yük taşımacılığı, parsiyel taşımacılık, otomotiv ürünleri taşımacılığı, dağıtım aracı depolama ve Teslimat Öncesi Muayene (PDI) gibi hizmetler sunmaktadır. ). Şirket, uluslararası taşımacılık alanında Chartering ve Multimodal Taşımacılık, Uluslararası Konteyner Taşımacılığı, Uluslararası Kara Taşımacılığı, Uluslararası Demiryolu Taşımacılığı ve Uluslararası Havayolu Taşımacılığı operasyonlarını yürütmektedir.
        <br />
        <br />
<b>Türkiye'de bir ilk: Borusan Limanı</b>
        <br />
        <br />
Borusan Limanı, Gemlik'te stratejik bir konuma sahip olup, dünya standartlarında genel kargo, proje kargo, dökme yük, PCC, liman ve terminal hizmetleri sunmaktadır. Borusan Limanı bugün 5.000.000 ton genel kargo, 400.000 TEU konteyner ve 300.000 araç elleçleme kapasitesine sahiptir. Limanın ayrıca 465.000 m² gümrüklü ve gümrüksüz terminal alanı bulunmaktadır. 635 m doğrusal iskelesi ve 14,5 m su derinliği Türkiye'de bir ilktir.
        <br />
        <br />
Borusan Lojistik, Yurt Dışı Taşımacılık Hizmetleri alanında ise %100 iştirakleri aracılığıyla Orta Doğu ülkeleri, Birleşik Arap Emirlikleri, Hollanda ve Kazakistan'da Borusan Logistics markası altında 3PL Lojistik Hizmetleri sunmaktadır. Borusan Lojistik, Aralık 2012'de sektörün en büyük 10 oyuncusundan biri olan Balnak'ı satın alarak lojistiğin çehresini değiştirdi.
        <br />
        <br />
Borusan Lojistik, küresel çapta 100'ün üzerinde acente edinerek uluslararası taşımacılık alanında güçlendi. Özellikle Avrupa'da son derece sağlam bir altyapı inşa eden Borusan Lojistik, kara taşımacılığı acentelik ağına şu ülkeleri de ekledi: Almanya, Avusturya, Belçika, Bulgaristan, Çek Cumhuriyeti, Danimarka, Fas, Finlandiya, Fransa, Hollanda, İngiltere, İspanya , İsveç, İsviçre, İtalya, Macaristan, Norveç, Polonya, Portekiz, Slovakya, Tunus ve Yunanistan. Borusan Lojistik'in taşımacılık alanında fark yarattığı diğer lokasyonlar arasında başta Kazakistan olmak üzere Orta Doğu ülkeleri ve Bağımsız Devletler Topluluğu yer alıyor.
        <br />
        <br />
<b>Lojistik hizmetlerinde dünyanın tercih edilen tedarikçisi</b>
        <br />
        <br />
Borusan Lojistik, 105 kara nakliye ofisi, 15 aktarma merkezi ve yaklaşık 600 araçlık filosuyla yurt içi kara taşımacılığı hizmeti vermektedir.
        <br />
        <br />
Yurt içi kara taşımacılığı, komple taşımacılık, parsiyel taşımacılık, özel dağıtım, oto taşıma, proje ve ağır yük taşımacılığı alanlarında hizmet veren Borusan Lojistik, günlük 3000'in üzerinde sefer ile Türkiye kara taşımacılığı sektörünün lideridir.
        <br />
        <br />
2015 yılında proje ve ağır yük hizmetleri çerçevesinde anahtar teslim enerji lojistiği hizmetleri vermeye başlayan Borusan Lojistik, artık sadece ulusal firmaların değil global firmaların da tercih ettiği bir tedarikçi haline geldi. Ayrıca şirket 550.000 m2 gümrüklü-boşluk kapalı alanı içerisinde depolama hizmeti de vermektedir. Borusan Lojistik'in yurt içi lojistik hizmetleri kapsamında sunduğu diğer hizmetler arasında üretim lojistiği ve araç lojistik merkezi hizmetleri de yer alıyor.
        <br />
        <br />
Araç lojistik merkezi hizmetleri kapsamında genellikle Köseköy/İzmit'te 200.000 m2 alan üzerine kurulu araç lojistik merkezinde verilen araç depolama ve teslimat öncesi muayene hizmetleri, müşterilerin araç lojistik merkezlerinde de verilebilmektedir. Borusan Lojistik, iş ortağı Bor Gümrük ile birlikte, elektronik olarak takip edilebilen, online erişimi kullanarak müşteriye özel rapor hazırlayabilen, tamamı lisanslı gümrük müşavirleriyle desteklenen gümrükleme hizmetlerinde hızlı ve güvenilir çözümler üretiyor. -başarmak ve gümrük web uygulamaları.
