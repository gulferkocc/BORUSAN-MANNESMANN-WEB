<h3>Borusan Grubu’nda Tedarikçilerle İlişkiler;</h3>

<li>Tedarikçilerin sunduğu mal ve hizmetlerin, şirketin kendi ürettiği mal ve hizmetlerin kalitesini doğrudan etkilediğine inanılır ve bu kapsamda tedarikçiler istenilen kalite ve standartlarda çalışan kurumlar arasından seçilir, dolayısıyla tedarikçiler iş ortağı olarak görülür.</li>
<li>Tedarikçi seçim sürecinde şeffaf ve tarafsız olunur.</li>
<li>Çalışılan ve potansiyel sayılabilecek tedarikçilere adil ve saygılı davranılır, yükümlülüklerin zamanında yerine getirilmesi için gereken özen gösterilir.</li>
<li>İş yapılan tedarikçilerin bilgileri özenle korunur.</li>
<li>Yasaları ihlal eden, iş etiğine uygun davranmayan tedarikçiler ile çalışılmaz. </li>
<li>Tedarikçiler, aracılar, alt yükleniciler yasalara ya da etik kurallara aykırı bir eylem gerçekleştirmek için kullanılamaz. </li>
<li>Çalışanların kendi objektif karar verme yetkinliklerini olumsuz etkileyecek hediyeleri kabul etmelerine izin verilmez. (Ayrıca bakınız: “Hediye Verme ve Kabul Etme Uygulama Esasları”) </li>
<li>Çalışanlar tedarikçilerle olan ilişkilerini yürütürken, üçüncü kişilerde menfaat ilişkisi bulunduğuna dair izlenimlerin oluşmasına imkân vermeyecek şekilde davranırlar.</li>

    

<h3>Sürdürülebilirlik Odaklı İş Birlikleri</h3>
 
  
 
<li> Holding ve Grup şirketleri olarak, benimsediğimiz sürdürülebilirlik yaklaşımı çerçevesinde Türkiye’de ve dünyada sürdürülebilirlik odaklı inisiyatiflerle iş birliği içerisindeyiz.</li>
<li>Borusan olarak 2006 yılında dünya genelindeki en büyük kurumsal sürdürülebilirlik girişimi olan Birleşmiş Milletler (BM) Küresel İlkeler Sözleşmesi’nin (UNGC) imzacısı olduk. Şirketlere, strateji ve operasyonlarını; insan hakları, çalışma standartları, çevreyi koruma ve yolsuzlukla mücadele odaklı evrensel prensiplere uyumlu hale getirmesi ve toplumsal hedeflere ulaşılmasına yönelik aksiyon almaları için çağrıda bulunan Birleşmiş Milletler Küresel İlkeler Sözleşmesi’nin 10 temel evrensel ilkesine uygun davranacağımızı taahhüt ettik.</li>
<li>2010 yılında İş Dünyası ve Sürdürülebilir Kalkınma Derneği’ne (SKD) Türkiye’den ikinci üye olarak katıldık. Dünya Sürdürülebilir Kalkınma İş Konseyi’nin (WBCSD – World Business Council for Sustainable Development) Türkiye’deki bölgesel ağı ve iş ortağı olan İş Dünyası ve Sürdürülebilir Kalkınma Derneği (SKD) çatısı altında gerçekleştirilen çalışmalara katılım göstermeye ve yönetim kurulunda yer almaya devam ediyoruz.</li>
<li>Birleşmiş Milletler Kadının Güçlenmesi Prensipleri (UNWEP’s), 2012 yılında Birleşmiş Milletler Küresel İlkeler Sözleşmesi (UN Global Compact) ve Birleşmiş Milletler Toplumsal Cinsiyet Eşitliği ve Kadının Güçlenmesi Birimi (UN Women) ortaklığında oluşturulan Kadının Güçlenmesi Prensipleri’ne (Women’s Empowerment Principles – WEPs) Türkiye’den ilk imza atan kuruluşlar arasında yer alan Borusan Holding, 2015 yılında Türkiye’den WEPs Liderlik Grubu’na seçilen ilk şirkettir.</li>
<li>TEİD Borusan Holding, Borusan Yatırım ve Borusan Mannesmann olarak, Türkiye’de iş etiği bilincinin artırılması, etik ve uyum kültürü oluşturulması amacıyla kurulan Etik ve İtibar Derneği’nin (TEİD) kurucu üyeleri arasında yer alıyoruz.</li>
<li>Deniz Temiz Derneği TURMEPA Borusan Holding olarak, ülkemiz kıyı ve denizlerinin korunmasını ulusal bir öncelik haline getirmek ve gelecek nesillere temiz denizleri ile yaşanabilir bir Türkiye bırakmak amacıyla projeler gerçekleştiren bir sivil toplum hareketi olan TURMEPA’nın üyesiyiz.</li>
<li>Çevre Dostu Yeşil Binalar Derneği (ÇEDBİK) Grup şirketlerimizden Borusan Mannesmann, sürdürdüğü çalışmalarla Türkiye’nin sürdürülebilir kentsel dönüşüm, enerji verimliliği ve yeşil bina konularında bilinçlendirilmesine önemli katkılarda bulunan Çevre Dostu Yeşil Binalar Derneği (ÇEDBİK) üyesidir.</li>
<li>TEMA Vakfı Borusan Holding, Borusan Grubu Kurucu ve Onursal Başkanı Asım Kocabıyık’ın da kurucuları arasında yer aldığı TEMA Vakfı’nın üyesidir. TEMA sürdürülebilir yaşam ilkesiyle başta topraklarımız olmak üzere doğal varlıkların korunması için, bilim temelli çalışan, topraktan gelen toplumsal barışa inanan, halkla bütünleşen, ülkenin ve dünyanın geleceğinde söz sahibi olan, gönüllü, bilinçli, öncü, uluslararası ve muteber bir sivil toplum kuruluşudur.</li>
<li>1993 yılından bu yana erken yaşlardan başlayarak her çocuğun potansiyelinin en üst seviyesine erişmesi için onu ve çevresini eğitim yoluyla desteklenmesini amaçlayan AÇEV (Anne Çocuk Eğitim Vakfı) ile iş birliğimiz kapsamında, tüm Borusanlılar için toplumsal cinsiyet eşitliği ile ilgili farkındalık yaratmak amacıyla seminerler düzenliyoruz.</li>

<h3>Sorumlu kaynak kullanımı</h3>
  <br />
    <br />
 

<p>Sürdürülebilirlik çalışmaları kapsamında, üretim, tüketim ve satın alma süreçlerimizde, sorumlu kaynak kullanımını önemsiyoruz. Geleneksel maliyet ve kalite yönetimi sürecine, tedarikçilerimiz için çalışma ön koşulu olarak benimsediğimiz “Borusan Grubu Tedarikçi Çalışma Koşulları Protokolü” belgesi için taahhüt talep ediyor ve dönemsel olarak tedarikçilerimizin çalışma koşullarını etkin iş birliği sayesinde denetim süreçlerinden geçiriyoruz. </p> 


