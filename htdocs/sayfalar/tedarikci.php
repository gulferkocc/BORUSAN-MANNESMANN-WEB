<h1>TEDARİKÇİLERİMİZ İÇİN</h1>
<h3>Sürdürülebilir Tedarik Zinciri Yönetimi ve Sorumlu Kaynak Kullanımı</h3>
   <br />
    <br />

<p>Borusan Mannesmann; güvenilir, sürdürülebilir, kaliteli ürün ve hizmetlerini, yetenekli ve yetkin iş gücüne, bağlı olduğu değerlerine ve tedarik zinciri yönetiminde sergilediği güçlü yönetim politikalarına borçlu. Bu değeri sürdürülebilir kılmak üzere Borusan Mannesmann olarak, çalışanlarımızın sağlığı, güvenliği ve gelişimine önem veriyor, tedarikçilerimizin teslimat, kalite, ürün uygunluğu gibi üretim süreç kontrollerini düzenli olarak sağlıyoruz. Bunun yanında, sürdürülebilirliğin tüm değer zincirimizde bir bütün olarak ele alınması gerektiği inancıyla, tedarik zincirimizi değer zincirimizin önemli bir parçası olarak kabul ediyor ve sektörde bu alana öncülük eden çalışmalara imza atıyoruz.

 
      <br />
    <br />
Faaliyet gösterdiğimiz tüm alanlarda etik kurallarından taviz vermeden çalışmalarımızı yürütüyor ve birlikte çalıştığımız tedarikçilerimizden aynı sorumlu yaklaşıma sahip olmalarını bekliyoruz.</p>

   <br />
    <br />

