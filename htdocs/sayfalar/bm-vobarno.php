   <h1>BM VOBARNO HAKKINDA</h1>
<p>BM Vobarno, Borusan Mannesmann Grubu'nun Avrupa bölgesindeki ilk iştirakidir. Avrupa otomotiv sektörünün ilk beş tedarikçisinden biri olan Vobarno tesisi, ürün yelpazesini genişleterek firmaların Halkalı İstanbul tesisi ile verimli bir sinerji yaratıyor.

