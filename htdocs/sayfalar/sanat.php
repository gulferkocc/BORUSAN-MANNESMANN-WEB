<h2>Avrupa'nın Gelecek Vaat Eden Orkestrası: BİFO</h2>
    <br />
<p>Türkiye'nin önde gelen senfoni topluluğu Borusan İstanbul Filarmoni Orkestrası (BİFO), 1999 yılında, Sascha Goetzel'in devraldığı ve Aykal'ın Onursal Şef olduğu 2008/9 sezonuna kadar BİFO'nun sanat yönetmenliğini ve baş şefliğini yapan Gürer Aykal yönetiminde kuruldu. BİFO, 2000/1 sezonundan itibaren düzenli olarak doğduğu şehrin her iki kıyısında da dünya yıldızlarına eşlik ederek konserler vermiş ve zamanla şehrin kültür ışıklarından biri haline gelmiştir. 2003-2018 yılları arasında İstanbul Müzik Festivali'nin de yerleşik orkestrası olarak görev yaptı. BİFO, ilk albümü Respighi, Hindemith Schmitt'i 2010 yılında yayınladı ve o yıl Salzburg Festivali'nin açılış şenliklerinde sahne aldı. Bunu 2012'de ikinci albümü Music from the Machine Age izledi ve üçüncü albümü Rimsky-Korsakov, Balakirev tarafından yapıldı. Erkin ve Ippolitov-Ivanov, 2014 yılında Goetzel yönetimindeki başarılı BBC Proms çıkışının hemen ardından serbest bırakıldı. BİFO ayrıca iki kayıt yayınladı; Carmen, 2016'da Ksenija Sidorova'yla ve Tchaikovsky, 2017'de Nemanja Radulović'le Deutsche Grammophon etiketi altında. Şubat 2016'da Avrupa turnesine çıkan BİFO, Ekim 2017'de ise Şubat 2017'de Hong Kong Sanat Festivali'nde sahne aldı.
</p>
