    <h2>Yıllardır sanatın sadık destekçisiyiz.</h2>
    
    <p>
        Borusan İstanbul Filarmoni Orkestrası'nın itici gücü olan Borusan Sanat, Türkiye'nin önde gelen sanayi gruplarından biri olan ve kuruluşundan bu yana sanatın büyük destekçisi olan ve Türkiye'nin modernleşme sürecine her zaman katkıda bulunmaya çalışan Borusan Holding tarafından kuruldu. Bir kişi veya kurumun yarattığı her değerin, ülkenin ekonomik ve sosyal büyümesine katkı anlamına geldiğini varsayan Holding, sanat anlayışının geliştirilmesi ve sürdürülebilirliği için bir sanat kurumunun temelini attı. Böylece 1997 yılında Borusan Sanat ortaya çıktı. Merkezi, İstanbul'un kültür ve sanat merkezi Beyoğlu'nun kalbinde yer alan, mimari açıdan büyüleyici bir yapı olan İstiklal Caddesi üzerinde yer alan Borusan Sanat,
        <br />
        <br />
Faaliyetlerinin merkezine Borusan İstanbul Filarmoni Orkestrası'nı (BİFO) yerleştiren Borusan Sanat, orkestranın ve BİFO grup liderlerinden oluşan Borusan Dörtlüsü'nün aylık düzenli konserlerini düzenliyor. Borusan Çocuk Korosu, çocuklarda klasik müzik sevgisini aşılamak amacıyla 2002 yılında başlatılan bir diğer anlamlı girişimdir. Sanat etkinliklerinin yanı sıra eğitim de düzenlemeyi amaçlayan Borusan Sanat, 2005 yılından bu yana genç müzisyenlere yurt dışında her yıl müzik bursu sağlıyor.
    </p>
