  <h1>  Ertuğ & Kocabıyık Yayınları</h1>
    <br />
<h2>Kültürel mirasımızı tüm dünyaya sunuyoruz</h2>
    
<p>Ertuğ & Kocabıyık Yayınları, önde gelen mimar ve fotoğrafçı Ahmet Ertuğ ve Borusan Holding Yönetim Kurulu Başkanı Ahmet Kocabıyık tarafından kurulmuş özel nitelikte bir yayınevidir. Yayınlanan nadir kitaplar, Anadolu'nun kültürel mirasına odaklanan eşsiz koleksiyon parçalarıdır. Ahmet Ertuğ'un muhteşem görüntülerine, içerdikleri başyapıtların doğasında var olan değeri yansıtacak şekilde dünyaca ünlü akademisyenlerin makaleleri eşlik ediyor. Ertuğ & Kocabıyık Yayınları'nın sanat kitapları, binlerce yıldır medeniyetlere ev sahipliği yapan bir coğrafyanın harikalarına yolculuğa açılan eşsiz kapılardır.
</p>

