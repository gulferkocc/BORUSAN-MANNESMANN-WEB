   <h1> Borusan Müzik Evi</h1>
    <br />
<h2>Sanatseverlerin Buluşma Noktası</h2>
   
<p>Borusan Müzik Evi, Borusan Sanat'ın faaliyetlerini daha geniş bir ölçeğe yaymak, hem sanatçılara hem de sanatseverlere farklı bir deneyim sunmak amacıyla 2010 yılında İstanbul'un sanat ve kültür merkezi İstiklal Caddesi'ndeki binada kuruldu. Borusan Müzik Evi, ilk üç yılında yeni müzik, dünya müziği, caz ve disiplinler arası performanslara odaklanan yoğun bir programla şehrin giderek gelişen sanat hayatına katkıda bulundu. Borusan Müzik Evi, müziğin farklı dallarından müzisyen ve toplulukları, bazıları ilk kez İstanbullularla buluşturdu. Borusan Müzik Evi, özellikle yeni müzik alanındaki etkinlikleriyle Borusan Sanat'ın klasik müziği ve çağdaş formları destekleme ve yaygınlaştırma hedefine büyük katkı sağladı. Müzik ve performans etkinliklerinin yanı sıra, Borusan Müzik Evi, Borusan Sanat'ın bağlı kuruluşlarından biri olan Sanat Merkezi/İstanbul için yıllık sergilere ve diğer küratöryel sergilere ev sahipliği yaparak görsel sanatların takipçilerini kendine çekti. Borusan Müzik Evi'nde düzenlenen sergiler arasında küratörlüğünü Richard Castelli'nin yaptığı “Matter-Light I” ve “Matter-Light II”, küratörlüğünü Suzanne Egeran'ın yaptığı “Cosmic Latte”, küratörlüğünü Marketa Haila'nın üstlendiği “Tidelines”, küratörlüğünü yaptığı “Revolution Revelation” yer alıyor. Mercan Dede ve Carlito Dalceggio ile Seçkin Pirim'in sergileri. Kasım 2014'ten itibaren Borusan Sanat'ın merkezi haline gelen Borusan Müzik Evi, son yıllarda özgün projeler sunarak faaliyetlerine devam ediyor. Bu projeler arasında Kod Müzik işbirliğiyle gerçekleştirilen “Nova Muzak Serisi”, Berlin merkezli sanat kurumu RadialSystem V işbirliğiyle “Berlin ve İstanbul'un Yeni Sesleri” ve XJAZZ İstanbul konserleri yer alıyor.</p>
