   <h1> Borusan Çocuk Korosu</h1>
    <br />
<h2>Geleceğin Sanatçıları ve Müzikseverleri: Borusan Çocuk Korosu</h2>
   
<p>Çocuk korolarının tarihsel olarak müzisyenlerin ve müzikseverlerin eğitiminde derin bir etkisi olmuştur ve birçok ünlü bestecinin geçmişi, müziğe olan sevgilerinin yeşerdiği çocuk korolarına kadar uzanmaktadır. Borusan Sanat da bu bilinçle bu iddialı projeyle bir ilke imza atmak üzere yola çıktı. Borusan Çocuk Korosu, koro ustası Gülsen Yavuzkal'ın öncülüğünde Kasım 2002'de kuruldu. Yaşları dokuz ile on dört arasında değişen çocuklardan oluşan koro, ulusal bayram kutlamaları, çocuk şenlikleri ve özel etkinlikler kapsamında sayısız konserler verdi. Koronun repertuarında farklı kültürlere ait şarkılar kendi dillerinde yer alıyor.
</p>
