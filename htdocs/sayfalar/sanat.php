   <h1> Borusan Dörtlüsü</h1>
    <br />
<h2>Ödüllü dörtlü: Borusan Dörtlüsü</h2>
    
<p>Borusan Quartet, 2005 yılında Maestro Gürer Aykal'ın rehberliğinde kuruldu. Daha sonra Alban Berg Quartet, Juilliard Quartet, Gürer Aykal, Joshua Epstein ve Maxim Vengerov'un ustalık sınıflarına katıldılar. 1776 Lorenzo Storioni kemanı, 1785 Ferdinando Gagliano kemanı, 2008 Stefano Conia viyola ve 1740 Petrus Guarneri viyolonsel gibi paha biçilmez enstrümanlar çalıyorlar. Kadıköy'deki Süreyya Operası ve İstanbul ENKA İbrahim Betil Oditoryumu'ndaki düzenli performanslarının yanı sıra, Quartet yurt içi ve yurt dışı konser ve turneleriyle yoğun bir programa sahip. Mayıs 2010'da ve aynı yıl Türkiye Andante dergisinin ilk Klasik Müzik Ödülleri'nde “Yılın Oda Topluluğu” ödülünü aldılar, Finali New York Carnegie Weill Resital Salonu'nda yapılan 2010 ICMEC Uluslararası Oda Müziği Yarışması'nda altın madalya kazandı. Adındaki bestecilerin yaylı dörtlülerinden oluşan ilk CD'leri Erkin, Aykal, Saygun Mayıs 2013'te, ardından İdil Biret'li Schumann'ın Piyano Beşlisi CD'leri Naxos'un IBA etiketiyle 2014'te, Mozart & Verdi ise Lila Müzik tarafından 2014'te yayınlandı. Temmuz 2016 ve son CD Company Eylül 2017'de Onyx tarafından yayınlandı. Repertuarları, Türk bestecilerin eserleri başta olmak üzere, klasik dönemden modern döneme kadar geniş bir yelpazeyi kapsıyor.
</p>
