<h3>Haydi Kızlar Kodlamaya</h3>
    <br />
    <br />
Borusan Mannesmann aynı zamanda Gemlik üretim üssünde toplumsal cinsiyet eşitliği bilincini yaygınlaştırmak ve kız çocuklarının güçlü bir destekle toplumsal yaşamda yer almalarını sağlamak üzere başlatılan “Haydi Kızlar Kodlamaya” adlı projenin yürütülmesinde rol alıyor.
    <br />
    <br />
Proje kapsamında, fabrika çalışanlarının 7-14 yaş aralığındaki kız çocuklarına 8 hafta süren bir eğitim sunuluyor. Eğitimler için İngiltere’de göçmen kız çocukları ve kadınların hayatın aktif bir parçası olabilmeleri için uzun zamandır kodlama eğitimi veren KızCode platformu ile iş birliği yapılıyor.
    <br />
    <br />
Program, kız çocuklarını bilişim teknolojileri ile yakınlaştırarak geleceğe hazırlamayı hedefliyor. Eğitimlerde öğrenciler elektroniğin temel prensiplerinden Scratch ile oyun, animasyon veya hikaye programlama ve 3D modellemeye kadar pek çok konuda ufuk açacak dersler görüyor.
    <br />
    <br />
 

<h3>Masada Değil Sahada </h3>
    <br />
    <br />
Ülkemizde mühendislik fakültesi mezunu kadınların büyük çoğunluğu, tercih edilmeyeceklerini ya da erkek adaylara göre şanslarının daha az olduğunu düşünerek fabrikalardaki ve üretimdeki mühendislik pozisyonlarına başvurmuyorlar. Şanslarını daha çok “masa başı” tabir edilen projelendirme pozisyonlarında deniyorlar. Borusan Mannesmann bu önyargıyı kırmak, mühendis kadınların sadece proje departmanlarında değil ağır sanayide üretim gibi erkek egemen görülen bir alanda da mesleklerini icra edebileceklerini göstermek, onları bu yönde cesaretlendirmek ve tesislerindeki kadın istihdamını arttırmak için bir iletişim kampanyası yürütüyor. Yılın belli dönemlerinde üniversite buluşmaları kapsamında, diğer dönemlerinde de sosyal medya ve işe alım sitelerinden duyurulan kampanyaya beklenenin üzerinde ilgi oluyor, mülakatlarda adaylardan özellikle tercih sebebinin bu kampanya olduğuna dair geri bildirim alınıyor.
    <br />
    <br />
 

<h3>Kreş Yardımı </h3>
    <br />
    <br />
Borusan Mannesmann saha çalışanlarını toplumsal eşitlik alanında destekleyerek, eşlerinin iş hayatına katılımını teşvik etmek amacıyla Kreş Yardımı uygulamasını yürütüyor. Bu uygulama ile eşi çalışan ve çocuğu kreş çağında olan (36 ay – 72 ay aralığında) üretim çalışanlarına ayda 289 TL tutarında Kreş Yardımı yapılıyor. Gemlik ve Halkalı tesisi çalışanlarının yararlandığı bu proje 2017 yılında Dünya Bankası’na bağlı Uluslararası Finans Kurumu (IFC)’nin küresel iş dünyasında kadını güçlendirme amaçlı iyi uygulamaları anlattığı son uluslararası raporunda örnek uygulama olarak yer almıştır.

 <br />
