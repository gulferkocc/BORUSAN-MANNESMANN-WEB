<b>Annemin İşi Benim Geleceğim</b>
    <br />
    
Kadının güçlendirilmesi çalışmalarını destekleyen Borusan Mannesmann, sanayide kadın istihdamının artırılması ve çalışan annelerin çocuklarına kaliteli bir eğitim sunulması için başlatılan Annemin İşi Benim Geleceğim projesinde yer alıyor. Şirket Aile ve Sosyal Politikalar Bakanlığı ve Bilim, Sanayi ve Teknoloji Bakanlığı iş birliği ile 10 ildeki 10 organize sanayi bölgesinde (OSB) “Borusan Neşe Fabrikası” adıyla, 0-6 yaş grubu çocuklara yönelik kreş ve gündüz bakım evleri açılmasına katkı sunuyor. “Annemin İşi Benim Geleceğim” sanayide daha fazla kadının iş yaşamına kazandırılarak kadının, ailenin ve ülke ekonomisinin güçlenmesine zemin hazırlıyor.
    <br />
    <br />
OSB’lerde 0-6 yaş grubu çocukların eğitim ve bakımı için kurulacak Borusan Neşe Fabrikaları ile;
    <br />
    
<li> Çocuk bakımı nedeniyle çalışamayan kadınlara imkan tanınması,</li>
<li> Kadının iş gücüne katılımının önündeki engellerin kaldırılması,</li>
<li> Sanayide kadın istihdamının artırılması,</li>
<li> Okul öncesi bakım ve eğitim hizmetlerinden yararlanamayan çocukların zihinsel ve fiziksel gelişimini sağlayacak bir eğitim ortamı sağlanması hedefleniyor.</li>
