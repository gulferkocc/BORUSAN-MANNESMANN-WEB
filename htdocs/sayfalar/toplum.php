<h3>Birlikte Eşit </h3>
    <br />
    <br />
Borusan Mannesmann özellikle üretim ve ağır sanayi alanlarındaki kadın istihdamının arttırılmasına öncülük etmek için 2018 yılında “Birlikte Eşit” projesini başlattı. Proje kapsamında Gemlik ve Bursa tesislerinde 22 kadın çalışan saha çalışanı olarak iş başı eğitimleri sonrasında erkek çalışanlarla aynı üretim hatlarında ve aynı görev tanımlarıyla çalışmaya başladılar. Günümüzde kadın istihdamını destekleyen birçok proje bu desteği üretimin yan alanlarındaki görev tanımlarıyla sınırlı tutarken bu projede çelik boru gibi bir ağır sanayi alanında kadın ve erkek çalışanlara gerçek anlamda bir eşitlik alanı yaratıldı. Ayrıca proje kapsamında ilk defa bir kadın çalışan Üretim Grup Lideri olarak atandı.</p>
