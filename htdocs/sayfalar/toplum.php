<h1>TOPLUMUMUZ İÇİN</h1>
<p> Borusan Grubu’nun ilk sanayi girişimi olan Borusan Mannesmann aynı zamanda grubun toplumsal sorumluluk odaklı kültürünün de temellerini oluşturmuştur. Borusan Grubu Kurucu ve Onursal Başkanı merhum Asım Kocabıyık’ın önderliğinde şekillenen bu anlayış ile Borusan Mannesmann bugün eğitim, kültür, sanat ve toplumsal cinsiyet eşitliği alanlarında kurumları ve projeleri destekliyor.
    <br />
    <br />
 

<b>Eğitime Destek</b>
Borusan Mannesmann, eğitime ve eğitim kurumlarının iyileştirilmesine yönelik çalışmalarını 1960'lı yılların başından itibaren sürdürüyor. Bu süreçte pek çok okul ve üniversiteyi destekleyen Borusan Mannesmann, Borusan Kocabıyık Vakfı çatısı altında eğitime sınırsız destek vermeye devam ediyor.

 <br />
    <br />

<b>Kültür ve Sanata Destek</b>
Borusan Mannesmann, Borusan Sanat çatısı altında ülkemizin sanat damarlarını besleyen Borusan İstanbul Filarmoni Orkestrası, Borusan Quartet, Borusan Müzik Evi ve Borusan Contemporary’nin destekçilerinden biri olmaktan gurur duyuyor.

 <br />
    <br />

<b>Toplumsal Cinsiyet Eşitliği Çalışmalarına Destek</b>
Borusan Mannesmann Borusan Eşittir inisiyatifiyle günümüzün en temel sosyal sorunlarından biri olan toplumsal eşitlik konusunda çözümün bir parçası olmak için çeşitli iş birliklerine imza atıyor ve projeler yürütüyor. 

