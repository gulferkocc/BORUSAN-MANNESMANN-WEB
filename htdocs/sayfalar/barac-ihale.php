    <h1>Borusan Araç İhale</h1>
    <p style="color:orangered">Türkiye'nin önde gelen ikinci el araç açık artırma platformu</p>
    <p>
        2001 yılında kurulan Otomax merkezli, Türkiye'nin ilk çok markalı yeniden pazarlama şirketi olan Borusan Araç İhale, nitelikli insan kaynağı ve güçlü iş ağı ile sektörün gelişimine ve kurumsallaşmasına katkıda bulunan ikinci el araç açık artırma hizmetleri sunmaktadır. Borusan Araç İhale, hizmetlerini sürekli olarak büyüyüp geliştirirken, yenilikçi teknolojileri kullanarak hizmet yelpazesini geliştirerek Türkiye'deki ikinci el otomobil pazarının gelişmesine ve kurumsallaşmasına katkıda bulunuyor.
        <br />
        <br />
Borusan Araç İhale, Gebze tesislerinde her hafta düzenli olarak fiziki ihaleler gerçekleştirmektedir. Müşterilere, Simulcast aracılığıyla fiziki ihalelere Türkiye'nin her yerinden online olarak katılma ve diğer fiziki katılımcılarla eş zamanlı olarak teklif verme imkanı sunulmaktadır. Müşteriler, fiziki müzayedelerin yanı sıra “7/24 İhale” online müzayede platformu üzerinden de Türkiye'nin her yerinden 7/24 araç alıp satabiliyor. Ayrıca ikinci el araçlara otomatik fiyatlama sağlayan “7/24 Fiyat” değerleme aracı da bulunmaktadır.
          <br />
        <br />
Borusan Araç İhale, araçların özel açık artırma seanslarına kayıt edilmesi konusunda muayene ve fiyatlandırma hizmetleri ile lojistik destek, alıcı ve satıcı arasındaki alım-satım işlemlerine ilişkin ödeme ve transfer hizmetleri sağlamaktadır.
          <br />
        <br />
Borusan Araç İhale'nin sisteme kayıtlı 2.300 kurumsal satıcı ve 7.800 alıcı üyesi bulunuyor. Şirket, Özel Açık Artırma Sistemi sayesinde yılda 25.000 adet ikinci el araç satışı gerçekleştirmektedir.
          <br />
        <br />
Borusan Araç İhale'nin düzenlediği açık artırmalardan yalnızca üye bağımsız ve franchise bayiler araç satın alabilirken, isteyen herkes araç satabilir. İhalelere katılan araçlar çoğunlukla filo şirketleri, operasyonel filo kiralama şirketleri, finans kuruluşları, sigorta şirketleri, serbest bayiler, franchise bayiler ve tüketiciler tarafından temin ediliyor.
          <br />
        <br />
Borusan Araç İhale hizmetlerinin sunduğu markalar için herhangi bir kısıtlama uygulanmamaktadır. Açık artırma yoluyla satılan araçlar Borusan Araç İhale garantisi altında sunulmaktadır. Borusan Araç İhale her marka ve her kategorideki otomobil üreticisi ve bayilerinin katılımına açık. Borusan Araç İhale, her markaya eşit davranan bağımsız yaklaşımıyla sektörde fark yaratmaya devam edecek.

    </p>
