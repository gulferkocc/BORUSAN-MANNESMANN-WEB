    <h2>Otonakit</h2>
     <div align="center">
     <img src="../img/Otonakit.jpg" /></div>
    <p>
        Otonakit, tüketicilere ikinci el araç satın alma seçeneği sunan; ikinci el araçlarını en hızlı ve güvenilir şekilde satmalarını sağlamak. Otonakit, Borusan Araç İhale'nin tüketicilere sunduğu bir hizmettir.
         <br />
        <br />
Yetkin insan kaynağı, güçlü alım-satım ağı ve İkinci El Oto sektöründeki engin tecrübesini birleştirerek; Borusan Araç İhale, tüketici segmentine hizmet etmek amacıyla Otonakit markasını yarattı.
         <br />
        <br />
Otonakit, Borusan güvencesiyle ikinci el otomobilini satmak isteyen herkese online fiyat vererek sürpriz ve sorun yaşamadan bir deneyim yaşatıyor ve anında ikinci el otomobil alımıyla sonuçlanıyor. Otonakit, Türkiye İkinci El Oto sektöründe 20 yıla yaklaşan deneyimi, bilgi birikimi ve gücüyle kullanıcılarına en gerçekçi ve güvenilir fiyatlandırmayı sunuyor.
         <br />
        <br />
Vizyonumuz, ikinci el otomobilini satmak isteyen her tüketiciye güvenilir, emniyetli, karlı, hızlı ve sorunsuz bir hizmet deneyimi sunmaktır. Misyonumuz, teknoloji kullanımı ve Borusan markasının güvenilirliği ile müşterilerine karlı, güvenli ve şeffaf bir iş ortamı sunmaya adanmış, müşteri odaklı bir platform sağlayarak İkinci El Otomobil Sektöründe sürdürülebilir ve sektör lideri bir şirket olmayı sürdürmektir.
