    <h1>ÜRETİM GÜCÜMÜZ</h1>

    <p>Borusan Mannesmann, üretimde mükemmellik hedefiyle 3 kıtadaki 11 tesisinde kaliteli ve yenilikçi ürünlere imza atıyor. Petrol, otomotiv, inşaat gibi pek çok farklı sektörde endüstrinin gelişimine hizmet eden yüksek teknoloji ürünleri, Borusan Mannesmann uzmanlığı ve güvencesi ile  birçok ülkeye ihraç ediliyor. 4.000’in üzerinde ürün çeşidine sahip olan Borusan Mannesmann aynı zamanda müşterilerinin ihtiyaçları ve beklentileri doğrultusunda ürün ve çözüm geliştirme kabiliyetiyle çelik boru alanında öncülüğü üstleniyor.   </p>
