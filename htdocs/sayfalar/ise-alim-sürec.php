    <p>Adaylar ilk görüşmede Yetenek Kazandırma Uzmanı ve ilgili pozisyonun yöneticisi ile mülakat gerçekleştirir. Bu görüşmede adayın yetkinlikleri alanında uzman kişiler tarafından ölçülür.</p>
      <h3>2.Görüşme</h3>
    <p>Departman yöneticisi ve ilgili İK iş ortağının katıldığı bu görüşmede adayı daha yakından tanımak amaçlanmaktadır.</p>
    <h4>Envanter ve Testler</h4>
    <p>Pozisyonun gerekliliklerine göre kişilik envanteri, yetkinlik testleri, İngilizce sınavı uygulanır.</p>
