    <p>Adaydan istenen vaka çalışması ile pozisyonun gerçeklerinin daha iyi anlaşılmasını sağlarken hem adayımız pozisyonu hem biz adayımızı daha kapsamlı bir şekilde tanıma imkanı yakalıyoruz.</p>
      <h3>3.Görüşme</h3>
    <p>Tüm süreçleri başarıyla tamamlayan adaylarımız, Genel Müdür ve ilgili genel müdür ile son görüşmeyi yaparlar.</p>
    <h3>Değerlendirme Anketi</h3>
    <p>Görüşmeler sonunda adaylarımıza işe alım süreciyle ilgili görüşlerini sorduğumuz bir değerlendirme anketi yapıyoruz. Bu sayede istihdam sürecinin aksayan noktalarını görebiliyor ve iyileştirme alanlarımızı belirliyoruz.</p>
