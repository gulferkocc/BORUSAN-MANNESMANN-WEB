 <h1>Borusan Mannesmann</h1>
    <p style="color:orangered">Borusan Mannesmann güçlü konumunu kurumsal yönetim yaklaşımına, nitelikli ve özverili iş gücüne, yeniliğe ve sürekli gelişime olan inancına borçludur. Şirketin kısa ve uzun vadeli planları arasında dünya pazarlarındaki konumunu güçlendirmek, katma değeri yüksek ürünler yaratmak, ürün kalitesini ve verimliliğini daha da artırmak yer alıyor.</p>
    <br />
    <p>
        Borusan Mannesmann, 1958 yılında Borusan Grubu'nun ilk sanayi kuruluşu olarak kuruldu. Borusan, 2000'i aşkın çalışanıyla Avrupa'nın önde gelen çelik boru üreticilerinden biri olan Mannesmann, bugün Türkiye'de pazar lideridir.
        <br />
        <br />
60 yılı aşkın boru üretim tecrübesiyle geniş bir ürün yelpazesi sunan Borusan Mannesmann, üretim faaliyetlerini 3 kıtada 7 tesiste gerçekleştiriyor. Bu tesisler yurt içinde Gemlik ve Halkalı'da, yurt dışında ise İtalya ve Amerika'da bulunmaktadır.
         <br />
        <br />
Borusan Mannesmann, çeşitli ürünleriyle başta inşaat, enerji ve otomotiv olmak üzere birçok sektöre hizmet veriyor. Ürün yelpazesi içerisinde; Su tesisat boruları, yüksek basınç boruları, genel amaçlı borular, doğalgaz ve petrol hattı boruları, sanayi boruları ve profilleri, sondaj boruları, betonarme çelik yapı profilleri bulunmaktadır.
         <br />
        <br />
Borusan Mannesmann, Halkalı fabrikasında özel üretim sanayi boru ve profilleri ile otomotivden mobilyaya, bisikletten beyaz eşyaya kadar birçok sektöre geniş bir ürün yelpazesi sunuyor. Gemlik'te inşaat sektörü başta olmak üzere birçok sektöre hizmet veren boyuna kaynaklı çelik boru fabrikasının yanı sıra aynı yerleşkede su, petrol ve gaz boru hattı projelerinde kullanılan spiral kaynaklı çelik boruların üretildiği bir fabrika daha bulunmaktadır. Teknoloji ve yetenekler açısından dünyanın önde gelen tesisleri arasında yer alan fabrika, Avrupa'da 24,5 m uzunluğunda spiral kaynaklı çelik boru üretebilen tek tesistir.
         <br />
        <br />
Avrupa'da otomotiv sektörüne yönelik üretim İtalya'daki Vobarno fabrikasında yapılırken, petrol ve doğal gaz arama ve çıkarma kuyularında kullanılan sondaj boruları 2014 yılında üretime başlayan Houston Texas fabrikasında üretiliyor. Borusan Mannesmann Pipe ABD; Şirket, Amerikan Metal Market tarafından verilen “Çelik Mükemmellik Ödülleri” kapsamında 2014 yılında “Yılın Teknoloji Sağlayıcısı” ödülünü, 2015, 2017 ve 2020 yıllarında ise “Yılın Boru Üreticisi” ödülünü kazandı. ABD çelik sektörünün en saygın ve bağımsız yayınlarından biri.
    </p>
   
