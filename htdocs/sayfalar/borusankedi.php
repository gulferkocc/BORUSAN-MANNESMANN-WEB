    <h1>Borusan Kedisi</h1>
    <p style="color:orangered">Borusan Cat, 1994 yılından bu yana Caterpillar'ın dünyadaki en iyi bayilerinden biridir. Borusan Cat, faaliyetlerini Türkiye'nin yanı sıra Azerbaycan, Gürcistan, Kazakistan, Kırgızistan ve Rusya'nın Uzak Doğu'sunda da sürdürüyor. Borusan Cat'in faaliyet gösterdiği ana sektörler inşaat, madencilik ve enerji&ulaşım sektörleridir. Şirket aynı zamanda Metso, Mantsinen, CDE, SEM, Exxon Mobil, MaK, EMD ve Berg Propulsion markalarının yetkili satıcısıdır.</p>
    <br />
   <p>
       Borusan Cat, amaç beyanı ile faaliyetlerine devam ediyor; “Daha İyi Bir Dünya İçin Çözümler Üretiyoruz” Borusan Cat, geniş ürün yelpazesiyle; makine, yeni ünite ve yedek parça satışı, satış sonrası destek hizmetleri, yeni Cat iş makinesi kiralama hizmeti, sertifikalı ikinci el iş makinesi konsepti ve benzersiz bir ikinci el hizmet. Borusan Cat, değer yaratan mühendislik hizmetleriyle enerji, denizcilik ve petrol işlerinde faaliyet gösteren şirketlere uçtan uca çözümler sunuyor.
       <br />
       <br />
Şirket, ileri teknoloji ve dijitalleşme yatırımlarına devam ederken, operasyonel mükemmelliği artırma ve optimize etme çalışmalarına da devam ediyor.
       <br />
       <br />
Borusan Cat, Borusan Cat Değişim Programı B'Daha ile aşınmış ve ömrünü tamamlamış bileşen ve parçalara yeni bir hayat veriyor. B'Daha, geniş komponent ve parça portföyü sayesinde maksimum çalışma süresi ve operasyonel verimlilik sağlarken, iş makinelerindeki arıza ve revizyon ihtiyaçlarınıza da en hızlı çözümleri üretiyor.
       <br />
       <br />
Borusan Cat müşterileri, ihtiyaç duydukları yedek parçalar hakkında bilgilendirilmekte ve online sipariş verebilmekte, Parts.cat.com (PCC) üzerinden satış noktalarına gelmelerine gerek kalmadan 7/24 yedek parça temin ve temin edebilmelerine olanak sağlanmaktadır. İnternet bağlantısı olan her cihazdan her an erişilebilen PCC sitesinde, parçaların fotoğrafları ve teknik özellikleri kolaylıkla görüntülenebilmekte ve mesai sınırlaması olmaksızın online sipariş verilebilmektedir.
       <br />
       <br />
SITECH, Trimble Navigasyon Limited'in büyük inşaat ve otoyol projelerine yönelik teknoloji çözümlerinin sağlanmasından sorumlu temsilcilerden oluşan yerel dağıtım kanalıdır. Bu içerikte; Türkiye'de SITECH Eurasia, Uzak Doğu Rusya'da SITECH DVI LLC ve Kazakistan, Kırgızistan ve Gürcistan'da SITECH Kazakistan geniş müşteri segmentlerine çözüm ortağı olarak hizmet vermektedir.
       <br />
       <br />
Yatırımlarına devam eden Revizyon Merkezleri'nde yatırımlarını sürdüren Borusan Cat, bunun ilk örneğini Kazakistan'ın Karaganda kentinde, iş makinaları ve makine parçalarının bakım ve revizyonlarının uçtan uca yapıldığı Revizyon Merkezleri'nde açtı. Borusan Cat'in Gebze'de 47.000 m2 ve Ankara'da 6.000 m2 olmak üzere iki Revizyon Merkezi daha bulunuyor.

