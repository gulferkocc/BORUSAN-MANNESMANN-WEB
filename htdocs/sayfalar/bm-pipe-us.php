       <h1> SON TEKNOLOJİ TESİSLER</h1>
<p>Houston'ın petrol ve gaz endüstrisinin kalbinde stratejik bir konuma sahip olan boru depomuz, sektördeki en iyi distribütörler için yılda 300.000 tondan fazla çelik boru üretmektedir. 2,375" ila 13,375" çaplardan 0,625" duvarlara kadar OCTG muhafaza ve hat borusu ihtiyaçlarınızı karşılayacak tam donanıma sahibiz. Gelişmiş lojistik desteğimiz, gerçek zamanlı envanter takibimiz ve titiz üçüncü taraf saha denetimlerimiz Borusan'la çalışmayı her adımda kolaylaştırıyor.
