<h1>MÜŞTERİLERİMİZ İÇİN</h1>
<p>Yüksek kalite standartları, yenilik geliştirme tutkusu ve müşteri memnuniyeti odaklı çalışma prensibi ile Borusan Mannesmann, müşterileriyle birlikte büyüyor ve gelişiyor. Bu kapsamda müşterilerinin beklenti ve ihtiyaçlarını dinlemeye özen gösteren Borusan Mannesmann, VoC (Müşterinin Sesi) ve müşteri memnuniyeti çalışmalarını büyük bir özenle yürütüyor.  </p>
   
    <h3>Kalite</h3>
    
    <p>Borusan Mannesmann’ın kalite politikası müşteri tatminine dayanır. Şirketin amacı, sipariş mektubunda belirtilen ilgili standartlara, teknik şartlara, özel ve uygulanabilir müşteri isteklerine uygun ürünler sunmaktır. Müşteri memnuniyetinin kalıcı olması için, müşteri şartlarının karşılanıp karşılanmadığı ve müşterinin değişen beklentileri ölçülüp değerlendirilerek tüm çalışanlara duyurulur, gerektiğinde ürün geliştirme ve belgelendirme çalışmaları yapılır, kullanıcıyı bilinçlendirmek için eğitici kurslar düzenlenir, sunulan ürünle ilgili en ufak memnuniyetsizlik ciddiye alınarak düzeltici faaliyette bulunulur.
        <br />
        <br />
Borusan Mannesmann’da çalışanların mutluluğu müşteri tatminine ulaşmada en önemli koşuldur. Kararlar, ilgili çalışanların görüşleri alınarak ve katılımcılık esasına göre alınır. Bireylerin yeterli bilgi ve beceriyle donatılarak işleriyle ilgili yetki ve sorumluluk üstlenmeleri sağlanır. Herkesin işini benimsediği, yaptığı işten mutlu olduğu bir ortam hedeflenir. Sorumluluk bireysel olmakla birlikte, ekip çalışmaları teşvik edilir, ekip başarısı bireysel başarının önünde tutulur.
        <br />
        <br />
Şirketin iç piyasada liderliğini koruması ve dış pazarlarda rekabet gücünü artırması için her karar ve faaliyette maliyet bilinci ile hareket edilerek tasarruf sağlanmasına önem verilir.
        <br />
        <br />
Tüm bu ilkelerin doğrultusunda, Borusan Mannesmann bulunduğu konumdan daha da iyiye ulaşmak için sürekli gelişme çabası içinde hareket etmektedir.
        <br />
        <br />
 <b>Laboratuvar Kalite Politikası</b>
        <br />
        <br />
 Borusan Mannesmann Kalite Laboratuvarı olarak verilen analiz hizmetlerimizde;
            <br />
        
<li> İyi mesleki ve teknik uygulamalar göstererek, sürekli eğitilen, becerisi geliştirilen ve yetkinliği sağlanmış uzman personeli ve teknolojinin gelişmesi sonucu ihtiyaç haline gelen değişimlere kaynakların sağlanması kabiliyeti ile hızlı şekilde cevap vererek ve kaliteyi en üst seviyede tutarak,</li>
<li> Ulusal/uluslararası Standart metotları kullanarak,</li>
<li> Referans standartların uluslararası ve laboratuvarlararası sisteme göre izlenebilirliğini temin ederek, laboratuvarlararası karşılaştırmalar yoluyla güvenilirliğini ve yetkinliğini sürekli kılacak şekilde, müşterinin ihtiyaç duyduğu ölçüm belirsizliklerini ihmal etmeksizin en küçük seviyede tutarak,</li>
<li> Hızlılık, doğruluk, süreklilik, güven, eşitlik, tarafsızlık ve gizlilik ilkelerinin topyekûn uygulanması ve müşterilerle yapılan işbirliğinin de yardımıyla, müşteri şikâyetlerinin azaltılması ve dolayısı ile müşteri memnuniyetinin artırılması konusunda gerekli tüm çalışmalar yapılarak laboratuvar faaliyetlerinin tutarlı olarak yapılması sağlanmaktadır.</li>
        <br />
        <br />
Bu bağlamda, analiz sonuçlarını olumsuz yönde etkileyen faktörlerin ortadan kaldırılması için Kalite Yönetim Sistemi içinde oluşturulan Laboratuvar Kalite El Kitabı, prosedürler, talimatlar ve diğer dokümanların ilgili tüm personel tarafından okunması, anlaşılması ve tüm uygulamalarında etkin şekilde kullanması gerektiği bilinci temin edilmiştir.
        <br />
        <br />
Laboratuvarımızın üst yönetimi; Kalite Yönetim Sistemi’ni tüm personelin katkısıyla TS EN ISO/IEC 17025:2017 standardı, müşteri şartı, yasal şartlar ve Akreditasyon Kuruluşu şartlarına uygun olarak etkin bir şekilde uygulayacağını ve sürekli iyileştireceğini taahhüt ederiz.

