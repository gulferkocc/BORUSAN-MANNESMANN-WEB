    <h1>ALPHA PROGRAMLARI</h1>
    <h2>Alpha Genç Yetenek Programı</h2>
    <p> 

91 yılından beri yürüttüğümüz ve The Peer Awards for Excellence’dan uluslararası ödül kazanan Borusan Alpha Genç Yetenek Programımız kapsamında, potansiyeli yüksek yeni mezun gençleri Borusan Grubu’na kazandırıp geliştirerek geleceğin liderlerini yetiştiriyoruz. Mevcut yılın Haziran/Temmuz döneminde lisans yada yüksek lisans eğitimini tamamlayabilecek öğrencileri değerlendirerek, adayların potansiyellerini ölçebileceğimiz değerlendirme araçlarını uyguluyoruz. Değerlendirme sonrasında tam zamanlı olarak işe alınan yeni mezunları 2 senelik yapılandırılmış bir gelişim programına dahil ederek geleceğin liderleri olma yolunda destekliyoruz.

Borusan Alpha Genç Yetenek Programı 2 senede bir düzenliyoruz. </p>

    <h2>Alpha Staj Programı</h2>

    <p>Borusan Alpha Staj Programımız kapsamında, üniversite 3. ve 4. sınıf ya da yüksek lisans öğrencilerini uzun dönem stajyer olarak değerlendiriyoruz. Bu program boyunca öğrenciler haftanın 2 ya da 3 günü stajlarını yaparken bir yandan da eğitimlerine devam ediyorlar. Alpha stajyerlerimiz, staja başladıkları ilk gün itibarıyla deneyimli Borusan çalışanlarıyla birlikte öğrenir, gelişir, projelerde yer alır ve katkı yaratır ve böylelikle kariyerlerine bir adım önde başlarlar.

Mezun olan stajyerleri Borusan Alpha Genç Yetenek Programı için mutlaka değerlendiriyor, Borusan yolculuklarının devam edebilmesi için onları destekliyoruz.</p>

