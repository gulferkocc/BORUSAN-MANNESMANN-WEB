    <h1> KURUCUMUZ</h1>
    <h2>Asım Kocabıyık<br />
Borusan Grubu Kurucu ve Onursal Başkanı<br />
(1924-2012)</h2>
    <div align="center">
   <img src="../img/hakkımızda-kurucumuz.jpg"  width="225" height="200" /></div>

    <p>1924 yılında Afyon’un Tazlar Köyü’nde doğan Asım Kocabıyık, ilköğrenimini ve ortaokulun ilk iki yılını Afyon'da, orta son sınıfı İstanbul'da, lise öğrenimini ise İstanbul Erkek Lisesi'nde tamamladı. Daha sonra girdiği İstanbul Üniversitesi İktisat Fakültesi'nden 1947 yılında mezun oldu.
        <br />
        <br />
İş yaşamı Türkiye'nin sanayileşme serüvenine paralel olarak anılan Asım Kocabıyık, 1944 yılında henüz öğrenci iken, girişimci babası Ahmet Kocabıyık'ın kurduğu İstikbal Ticaret T.A.Ş.’nde kurucu ortak ve yönetim kurulu üyesi olarak görev aldı ve çalışmaya başladı. Kocabıyık, başlangıçta demir-çelik ithalatı ve tarım ürünleri ithalatı yapan İstikbal Ticaret’te 8 yılı yönetim kurulu üyeliği, 49 yılı yönetim kurulu başkanlığı olmak üzere 57 yıl görev yaptı. Asım Kocabıyık’ın babası ile birlikte temelini attığı şirket, aynı zamanda bugünkü Borusan Grubu’nun çekirdeğini oluşturdu.
        <br />
        <br />
Borusan Grubu’nun ilk sanayi girişimi olan Borusan Boru’yu kurarak 1958 yılında 27 çalışanı ile sanayi sektörüne adım atan Kocabıyık, bugün 9.000'e yakın kişinin çalıştığı çeşitli sanayi kuruluşlarını bünyesinde barındıran ve önemli distribütörlük faaliyetlerinin yürütüldüğü Borusan Grubu’nun kurucusudur.
        <br />
        <br />
Özellikle eğitim alanında yoğunlaşan sosyal sorumluluk faaliyetlerini "Memlekete bir hizmet borcum var. Onu yerine getirmeye çalışıyorum" sözleriyle izah eden Kocabıyık, vefatına dek eğitim, kültür, sanat ve çevre alanında toplumsal çalışmalara imza attı.
        <br />
        <br />
Asım Kocabıyık ayrıca, Türkiye Aile Sağlığı ve Planlaması Vakfı, TEMA Vakfı, Eğitim Gönüllüleri Vakfı, Türk Musikisi Vakfı ve Afyon Eğitim Vakfı’nın da kurucuları arasında yer aldı. 24 yıl İstanbul Sanayi Odası, 15 yıl TÜSİAD yönetiminde bulundu, 4 yıl da İktisadi Kalkınma Vakfı’nda başkanlık yaptı.
        <br />
        <br />
Asım Kocabıyık, başta İstanbul Üniversitesi olmak üzere çeşitli yüksek öğrenim kurumlarınca fahri doktora unvanına layık görüldü. Kocabıyık, Devlet Üstün Hizmet Madalyası ve İtalyan Hükümeti’nin verdiği şövalye nişanına layık görüldü.</p>

