     <h1>  Borusan Holding</h1>
<h3>Borusan Grubu'nun ana stratejisi, küresel pazarlara odaklanan, yenilikçi ürün ve hizmetler yaratan bir zihniyetle Türkiye ekonomisine katma değer yaratmaya devam etmek üzerine kuruludur.</h3>
   
<p>2021 yılında 77. yılını kutlayan Borusan Grubu, Türkiye başta olmak üzere dünyanın çeşitli pazarlarında çelik, distribütörlük, lojistik ve enerji sektörlerinde istikrarlı bir şekilde büyümeye devam ediyor.
    <br />
    <br />
Borusan Grubu'nun bu başarısının arkasında güçlü kurumsal iş kültürü yatıyor. Borusan, müşterilerinin ihtiyaçlarını anlamak için onlara yönelik yenilikçi, farklı ve ekonomik bir çözümü tutkuyla geliştirmeye kararlıdır. Borusan, hızlı ve esnek aksiyon alma becerisi sayesinde, risk korkusu olmadan, bulunduğu pazarlara yön verecek yenilikçi ürün, hizmet ve iş modelleri sunuyor.
    <br />
    <br />
Bu çerçevede Borusan, iç girişimcilik, yenilikçilik, uluslararası pazarlarda aktif rekabet stratejisi ve topluma, ekonomiye ve çevreye maksimum fayda sağlama misyonuyla fark yaratan çalışma ve projeler ortaya koyuyor.
    <br />
    <br /></p>


<h4>Uluslararası Başarı</h4>

<p>Borusan Grubu, dünyanın dört bir yanından güçlü şirketlerle uluslararası ortaklıklar sürdürüyor. Grubun dünyanın önde gelen çelik üreticisi ArcelorMittal, dünyanın en büyük sanayi kuruluşu Salzgitter Mannesmann, otomotiv devi BMW AG ve Almanya'nın üçüncü büyük enerji kuruluşu EnBW AG ile başarılı ortaklıkları bulunmaktadır.</p>
    <br />
    <br />
<h4>Daha İyi Bir Yarın için Çalışmak</h4>

<p>Borusan Grubu, faaliyet gösterdiği her sektörde sürdürülebilirlik ve çevre duyarlılığı konusunda öncü olmayı misyon edindiği inancındadır. Gezegenin doğal kaynaklarının sınırlı olduğunun bilincinde olan Borusan Grubu, yaşamı iyileştirmeyi amaçlıyor. Kaynakları verimli kullanarak ve geleceği israf etmeden standart hale getirmek.</p>
    <br />
    <br />
<h3>Borusan, Türkiye'de gelişen ve büyüyen bir iş grubu olarak eğitim, sanat/kültür ve toplumsal eşitlik alanlarında kalıcı sosyal sorumluluk projeleri yaratmaya devam ediyor.</h3>
    
<p>Eğitim alanında, Borusan Holding'in kurucusu ve Onursal Başkanı merhum Asım Kocabıyık tarafından 1992 yılında kurulan Borusan Kocabıyık Vakfı, okullar inşa edip Milli Eğitim Bakanlığı'na devretmeye devam ederken, okulların her türlü malzeme ihtiyacını karşılıyor ve bağış yapıyor. Maddi imkanları sınırlı olan başarılı öğrencilere burs verilmektedir.
    <br />
    <br />
Kültür-sanat alanında 1997 yılında faaliyetlerine başlayan Borusan Sanat, Borusan İstanbul Filarmoni Orkestrası, Borusan Quartet, Borusan Müzik Evi ve Borusan Çocuk Korosu ile birlikte düzenlediği müzik etkinliklerini kesintisiz sürdürüyor. Perili Köşk, 2010 yılından bu yana Borusan Contemporary çatısı altında çağdaş sanat eserlerine ev sahipliği yapıyor ve aynı zamanda Holding'in genel merkezi olarak hizmet veriyor.
    <br />
    <br />
Ayrıca Borusan Grubu, Birleşmiş Milletler Kadının Güçlenmesi İlkeleri'nin (UN WEP) ilk imzacıları arasında yer alıyor ve bu doğrultuda toplumsal cinsiyet eşitliği konusunda önemli projelere imza atıyor. Borusan, 'Annemin İşi Benim Geleceğim' projesi ve Mikrofinans Şubeleri ile kadınların güçlenmesi ve iş hayatında fırsat eşitliğine sahip olması için çalışıyor.</p>
