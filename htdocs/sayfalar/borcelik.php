    <h1>Kerim Çelik</h1>
    <p style="color:orangered">Kerim Çelik, 50 yılı aşkın tecrübesiyle Türkiye'de Çelik Servis Merkezi hizmetini uygulayan ilk firmadır.</p>
    <br />
    <p>
        Borusan Holding ve Arcelor Mittal'in ortak girişimi olan Kerim Çelik, 1960 yılında kurulmuş ve yaklaşık yarım asırlık hizmet süresinde Türk sanayisinin tercih ettiği lider yassı çelik servis sağlayıcısı olmuştur.
          <br />
        <br />
1977 yılında faaliyete geçen toplam 500.000 ton üretim kapasiteli Çelik Servis Merkezi ile dünya standartlarında hizmet vermeye devam etmektedir.
          <br />
        <br />
Kerim Çelik, otomotiv alt yüklenicilerine, beyaz eşya ana ve suntacilerine, ısıtma - soğutma ve havalandırma, elektrik, aydınlatma, elektronik, inşaat, asansör, makine ve yedek parça, radyatör, silo, mobilya ve aksesuar imalatçıları, kablo kanalları, boru&profil başta olmak üzere birçok sektöre hizmet vermektedir.
          <br />
        <br />
Teknik destek ve ürün geliştirme alanlarında da uzmanlaşan Kerim Çelik, müşterilerine en kaliteli hizmeti sunmak amacıyla, Bursa, Manisa ve Adana gibi pazarın önde gelen sanayi bölgelerinde müşterilerine en iyi hizmeti sunmak için yoğun bir şekilde çalışmaktadır.
    </p>
