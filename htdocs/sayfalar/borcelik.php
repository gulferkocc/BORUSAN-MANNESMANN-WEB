    <h1>Borçelik</h1>
    <p style="color:orangered">Butik üretimde büyük sanayinin en önemli tedarikçisi olarak konumlanan Borçelik, liman avantajına sahip konumu itibarıyla da stratejik öneme sahip.</p>
    <br />
    <p>1990 yılında Borusan Holding ve AcerlorMittal ortaklığıyla yönetilen Borçelik, Türkiye'nin en büyük üretim kapasitesine sahip en modern galvanizli sac üreticisidir.
        <br />
        <br />
Borçelik, Gemlik'te 240 bin m2 alan üzerinde faaliyet gösteriyor ve sıcak daldırma galvanizli çelik, soğuk haddelenmiş tavlanmış çelik ve asitlenmiş & yağlanmış sıcak haddelenmiş çelik kategorilerinde üretim yapıyor.
        <br />
        <br />
1994, 2003 ve 2008 yıllarında toplam 500 milyon ABD Doları tutarındaki yatırımlarla üretim kapasitesi 1,5 milyon tona çıkarılmıştır. 900 bin galvanizli sac, 600 bin soğuk haddelenmiş sac kapasitesine sahiptir.
        <br />
        <br />
Ayrıca ticari, çekme, derin çekme ve ekstra derin çekme çelikleri, dual faz, fırında sertleştirme, refosforize, HSLA, emaye ve yapısal kaliteler kategorisinde yer alan ürünler Gemlik'teki fabrikada yüksek teknoloji ile üretilmektedir. teknoloji.
        <br />
        <br />
Otomotiv sektöründe özellikle dış ve iç yüzey çelik sac üretiminde ArcelorMittal'in Extragal galvanizleme teknolojisini kullanan Borçelik, otomotiv sektöründe görünür yüzey çelik sac üretebilen ilk Türk kuruluşudur. Böylece otomotiv sanayine Avrupa Birliği standartlarında endüstriyel girdi sağlanırken, bu konuda da dışa bağımlılık azaltılıyor.
        <br />
        <br />
Yurt içi pazardaki ana müşteri grupları arasında beyaz eşya, otomotiv ana ve yan sanayi, panel radyatör, inşaat, boya kaplama, boru ve profil, ambalaj, metal eşya sektörleri ve çelik servis merkezleri yer alıyor.
</p>
