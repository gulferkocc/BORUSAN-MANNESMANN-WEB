    <h1>HAKKIMIZDA</h1>

    <p>

        2023 yılında 65. kuruluş yılını kutlayan Borusan Mannesmann, Türkiye’nin öncü güçlerinden biri olan Borusan Grubu’nun ilk sanayi girişimidir. Kurulduğu ilk günden bu yana global bir vizyona sahip olan şirket, bu doğrultuda 1998 yılında Avrupa'nın lider çelik ve teknoloji kuruluşu Salzgitter Mannesmann GMBH ile faaliyetlerini birleştirdi.
        <br />
        <br />
 Bugün Borusan Mannesmann, yaklaşık 2800'den fazla çalışanı ve 4.000’den fazla ürün çeşidi ile güvenilir bir dünya markası olarak faaliyetlerini sürdürüyor. 3 kıtadaki 11 tesisi ve yüksek satış hacmi ile çelik boru sektöründe Avrupa'nın ve dünyanın önde gelen üreticileri arasında yer alıyor.
        <br />
        <br />
Borusan Mannesmann, otomotivden inşaata, enerjiden makine üretimine dek her alanda yüksek teknoloji ürünü borularıyla deneyimini, uzmanlığını ve tutkusunu dünyanın dört bir köşesine taşıyor. Pazar dinamikleri doğrultusunda küresel bir bakış açısıyla yatırımlarını sürdüren şirket 2001 yılında İtalya’da aldığı tesisle Borusan Mannesmann Vobarno Tubi S.p.A. şirketini kurarak ilk yurt dışı yatırımını yaptı. Ardından 2014 yılında ABD’deki Houston Baytown fabrika yatırımını yönetmek üzere Borusan Mannesmann Pipe US Inc.’yi kurdu. Yüksek teknolojisi ve yenilikçi ürünleriyle kısa sürede başarıyı yakalayan şirket, 2016, 2017 ve 2020 yıllarında ABD’nin en prestijli yayınlarından American Metal Market tarafından “En İyi Boru Üreticisi” ödülüne layık görüldü. 2023'de global pazarların yerel oyuncusu olma stratejisi kapsamında ABD’nin en yüksek kalite ve kapasitede üretim yapan şirketlerinden Berg Pipe’ı satın aldı.​
        <br />
        
Bu atılımlarıyla Türkiye’nin öncü yurt dışı yatırımcıları arasına giren Borusan Mannesmann potansiyeline inandığı ürün gruplarında farklı ülkelerde yatırım fırsatlarını kolluyor ve hem şirketin hem de Türkiye’nin rekabet gücüne ivme kazandırmayı hedefliyor.
        <br />
        <br />
Amerika, Avrupa, Afrika ve Asya'nın çeşitli ülkelerinde yaptığı ihracat ile ülkemize sağladığı katkıların yanı sıra, gelecek yıllar için verdiği gelişim güvencesi ile de Türkiye ekonomisinin itici güçlerinden birini oluşturuyor.
        <br />
        <br />
Türkiye'nin en büyük sanayi kuruluşları listesinde 50 yıldır ilk 100'de yer alan Borusan Mannesmann; binlerce ürünü, güvenilir hizmeti, kalitesi, Türkiye ve dünyada gerçekleştirdiği ilkler ile boru üretmenin ötesinde Türkiye'nin geleceğini inşa ediyor. Yönetim politikaları ile sürdürülebilir bir toplum ve dünya, büyük ölçekli yatırımları ile kalkınmış bir ülke ve güvenli bir gelecek sunuyor.

