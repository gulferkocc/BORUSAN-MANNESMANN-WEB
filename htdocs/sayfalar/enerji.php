 <h1>Enerji | Borusan Mannesman</h1>
        <p>Borusan Mannesmann olarak 65 yıllık tecrübemizle, dünyaca ünlü enerji projeleri için hat borusu, sondaj borusu, doğal gaz ve yüksek basınç boruları üretiyoruz. Katma değerli ürünlerde büyüme stratejimiz doğrultusunda enerji sektörü için Türkiye’de Gemlik’te bulunan iki tesisimizle, Amerika’da ise Florida, Alabama ve Teksas’ta bulunan tesisimizle uzmanlığımızı dünya ile paylaşıyoruz.
            <br />
                 <br />
Ülkemizin en büyük spiral kaynaklı boru entegre üretim tesisi olan Gemlik Fabrikamız dünyadaki ilk üç 2-adımda kaynak teknolojisiyle üretim yapan ve Avrupa’nın tek parça 24,5 metre üretim yapabilen tek tesisi olma özelliğini taşıyor. Gemlik  Elektrik Direnç Kaynaklı (ERW) Boru  Fabrikamız ise özellikle gaz borularındaki deneyimi ve uzmanlığı ile sektörün ana üretim üslerinden biri olarak görülüyor.
     <br />
                 <br />
2014 yılında Amerika’da kurulan Houston Fabrikamız ise özellikle petrol, doğal gaz arama ve çıkarma kuyularında (OCTG) ve nakil hatlarında kullanılan çelik borular üzerinde uzmanlaşarak, ileri teknoloji donanımıyla ve ödüllü tesisiyle müşterilerine anahtar teslim proje çözümlerini en rekabetçi şartlarda sunmaya devam ediyor.
     <br />
                 <br />
Borusan Mannesmann olarak altyapı hat boruları alanında dünyaca ünlü projeler ve özellikle Kuzey Amerika pazarında geçtiğimiz 10 yıl içerisinde edinilen deneyimlerle bu segmentteki uzmanlığımızı tüm dünyayla paylaşıyoruz. API standardındaki ürünlerde ABD’nin Türkiye’deki tek tedarikçisi olarak görülen Borusan Mannesmann proje yönetiminde; satış öncesi planlamadan satış, üretim, montaj ve kalite süreçlerine kadar yüklenici firmaların çözüm ortağı olarak yer alıyor.
     <br />
                 <br />
Sektörde HSAW boru, büyükk çaplı doğal gaz borusu, spiral doğal gaz borusu, kaplamalı doğalgaz borusu, API büyük çaplı boru adlarıyla da bilinen, farklı kullanım alanlarına göre özelleştirilen ve kalite standartlarına uygun olarak minimum toleransla üretilen Spiral Kaynaklı (HSAW) Hat Boruları, projenin veya kullanım alanının gerektirdiği şekilde şekilde et kalınlığı, çap ve kaplama seçeneklerine sahip olarak üretilebilmektedir. 
     <br />
                 <br />
Mobile, Alabama ve Panama City, Florida'da bulunan tesislerimizde ürettiğimiz Boyuna Tozaltı Kaynaklı (LSAW) Hat Boru​larımız ile enerji sektöründe ektrem koşullardaki nakil hatları için yüksek dayanıklılıkta ve kalitede çözümler sunuyoruz.
                 <br />
                 <br />
Farklı kullanım alanlarına göre özelleştirilen ve kalite standartlarına uygun olarak minimum toleransla üretilen Boyuna Kaynaklı (ERW) Hat Boruları, projenin veya kullanım alanının gerektirdiği şekilde şekilde et kalınlığı, çap ve kaplama seçeneklerine sahip olarak üretilebilmektedir. Dikişsiz boruya çok güçlü bir alternatifdir.
                 <br />
                 <br />
OCTG borusu olarak da bilinen sondaj borularımızı, hem casing hem tubing için, API 5CT’ye göre düz uçlu ve API spec. 5B’ye göre dişli manşonlu üretim olanaklarımızla beraber %100 kaynak bölgesi normalizasyonu, %100 ultrasonik kaynak dikiş kontrolü, %100 hidrotest, %100 dış çap kontrolü, iç çapaksız üretim, homojen et kalınlığı, daraltılmış tolerans limitleri ile en üst düzey yetkinlikte ürettiğimiz borularımızı müşteri taleplerine uygun alternatif drift test opsiyonları ile Borusan Mannesmann bünyesinde TS EN 17025’e göre akredite laboratuarlarda testlerini yaparak teslim ediyoruz.
                 <br />
                 <br />
Doğal Gaz borularımız müşterilerimizin talep ettiği sektörel standartlar ve kalite gerekliliklerinde üretilmektedir. Doğal Gaz borularımızın ağızları kaynak ağızlıdır ve standart olarak 30 derece olsa da ihtiyaca göre 45 ve 60 derece olarak üretilebilmektedir. PE kaplı ve polietilen kaplı olarak da üretimi yapılabilmektedir.
                 <br />
                 <br />
Kazan borusu, buhar borusu, yüksek sıcaklık borusu ve yüksek basınç borusu olarak da bilinen borularımızın üretimi müşterilerimizin talep ettiği sektörel standartlar ve kalite gerekliliklerinde yapılmaktadır. Çekme çelik borulara alternatif olarak kullanılabilmektedir.</p>
