    <p>
        1950’li yıllardan itibaren Türkiye de boru sanayiinde gelişmeler yapıldığına tanık oldu. Bugün özellikle çelik boru alanında dünyada önemli bir konuma yükselmiş olmasında, üretim teknolojisi ve standartlarıyla uluslararası rekabet koşullarını yerine getirmesinde ve kendi teknolojisini üretebilmesinde vardığı nokta, Borusan Mannesmann’ın başarı öyküsünün paralelinde gelişti. Bu bakımdan Borusan Mannesmann’ın 60 yıllık yolculuğu Türkiye’de boru üretiminin ve dolayısıyla da sanayinin gelişimini de yansıtıyor.
           <br />
