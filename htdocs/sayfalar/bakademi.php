    <h3>Liderlik Fakültesi</h3>
     <p>Borusan Grubu'nun büyümesi ile birlikte insan kaynakları gereksinimleri de gün geçtikçe artmaktadır. Bu ihtiyacın büyük bölümünü, kendi çalışanlarımız arasından yaratmak, Borusan Grubu'nda değişen koşullarda; bilgiyi yöneterek stratejiyi belirleyen, kaynakları sonuca yönelik harekete geçiren, kendini ve ekibini sürekli geliştiren ve değişimi yöneten iş liderleri yetiştirmek amacıyla kurulmuştur. 3 seviyede ve başlangıçtan itibaren 6 yılda tamamlanan, Pre-MBA'e eşdeğer dersleri ve Borusan Grubu'na özel iş pratikleri ile zenginleştirilmiş, uzman eğitmen kadrosu, özel konuk ve konuşmacıların katkıda bulunduğu program, Sabancı Üniversitesi işbirliği ile yürütülmektedir.</p>
     
