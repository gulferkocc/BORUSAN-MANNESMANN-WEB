    <h3>Satış Fakültesi</h3>
    <p>Borusan Grubu'nun rekabetin sürekli arttığı ortamda, ürün ve hizmet kalitesinin yanında, müşteri odaklı yaklaşımı ile değişen müşteri ihtiyaçlarını ve beklentilerini karşılayan, işinde fark yaratarak sürekli büyümeyi gerçekleştirecek satış ekiplerini yetiştirmek amacıyla kurulmuştur. Uzun soluklu bir program olarak, sürekli gelişen içeriği ve uzman eğitimci kadrosu, vaka çalışmaları ile zenginleştirilmiş uygulamalı faaliyetleri ile Sabancı Üniversitesi iş birliği ile yürütülmektedir.</p>
     
    <br /> 
       
