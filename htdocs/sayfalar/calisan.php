    <p>Yalın 6 Sigma, Borusan Mannesmann’da DAİMAY6S mottosu çerçevesinde değerlendirilir. (<b>D</b>estekleyici, <b>A</b>nlam Katan,<b> İ</b>şin İçinde,<b> M</b>üşteriye Yönelik, <b>A</b>nda) Her bir harf Yalın 6 Sigma metodolojisinin organizasyona kattığı bir değer önermesini simgeler. DAİMA Y6S, Yalın 6 Sigma metodolojisinin yalın yaklaşımla ele alınarak uygulandığını, organizasyonun her katmanına anlatmak için akılda kalıcı bir slogan haline getirdiğimiz temel yayılım yaklaşımımızdır.
            <br />    <br />
2002 yılından 2017 yıl sonuna kadar Borusan Mannesmann Yalın 6 Sigma metodolojisini kullanarak müşterilerinin sesini dinleyerek, kritik müşteri beklentilerini karşılayacak şekilde süreçlerini iyileştirmeye yönelik, kalite ve hizmet anlayışını daha da geliştiren 200 Siyah Kuşak, 477 Uzman Yeşil Kuşak projesi gerçekleştirdi. Bu projelerden, yalnızca projenin bitimini takip eden bir yıl içerisinde elde edilen toplam getiri yaklaşık 32,8 milyon dolar olarak kayıtlara geçmektedir.
            <br />    <br />
<b>Yalın 6 Sigma Yol Haritaları</b>
            <br />    <br />
Borusan Mannesmann’da Yalın 6 Sigma projeleri ihtiyaca göre 4 farklı yol haritası kullanılarak hayata geçirilmektedir.
            <br />    <br />    <br />    <br />
<b>DMAIC</b> yol haritası, mevcut süreçlerin iyileştirilmesi hedeflendiğinde kullanılmaktadır. İngilizce Define, Measure, Analyze, Improve, Control kelimelerinin kısaltmasından oluşan bu yol haritasının adımları Türkçe’ye Tanımlama, Ölçüm, Analiz, İyileştirme ve Kontrol olarak çevrilmektedir.
            <br />    <br />
<b>DMEDI</b> yol haritası, yeni ürün ve süreç yaratılması hedeflendiğinde kullanılmaktadır. İngilizce Define, Measure, Explore, Develop, Implement kelimelerinin kısaltmasından oluşan bu yol haritasının adımları Türkçe’ye Tanımlama, Ölçüm, Araştırma, Geliştirme ve Uygulama olarak çevrilmektedir.
            <br />    <br />
<b>Yalın (Değer Akış)</b> yol haritası 8 israfı (kullanılmayan zeka, hata, stok, fazla üretim, bekleme, hareket, taşıma, fazla işlem) önlemek ve katma değeri yüksek aktiviteleri oransal olarak yukarı taşımak amacıyla kullanılmaktadır.
            <br />    <br />
<b>Kaizen</b> yol haritası ise kapsamı nispeten daha dar olan konuların daha kompakt bir yaklaşımla kısa zamanda çözüme kavuşturulmasını hedefleyen bir proje yapma yoludur.
            <br />    <br />
Borusan Mannesmann, iş süreçlerinde maliyetleri ve hata oranını düşürmek, verimliliği artırmak, sürekli iyileştirmelerle daima artan bir müşteri memnuniyeti sağlamak için Yalın 6 Sigma metodolojisini uygulamaya ve tüm paydaşları için değer yaratarak büyümeye devam ediyor.</p>
