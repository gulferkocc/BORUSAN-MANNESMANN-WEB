ÇALIŞANLARIMIZ İÇİN</h1>
    <p>Borusan Mannesmann, güvenilir, sürdürülebilir ve kaliteli ürün ve hizmetlerini yetenekli ve yetkin iş gücüne, bağlı olduğu değerlerine ve güçlü yönetim politikalarına borçlu. Bu değeri sürdürülebilir kılmak üzere Borusan Mannesmann çalışanlarının sağlığı, güvenliği ve gelişimine önem veriyor ve sektörde bu alana öncülük eden çalışmalara imza atıyoruz.</p>
    <h3>İş Sağlığı Güvenliği ve Çevre Politikası</h3>
    <p>Ana iş kolu olan çelik boru sektöründe Avrupa’da da söz sahibi olan Borusan Mannesmann’ın tüm çalışanları ve yönetimi, çalışanlarına, müşterilerine, tedarikçilerine, stajerlerine ve tüm ilgili taraflara daha güvenli ve sağlıklı iş ortamı yaratmak, yaralanmalar ve sağlık bozulmalarını önlemesi için güvenli ve sağlıklı çalışma koşullarının sağlamak; çalışanların danışma ve katılımları ile  “0 iş kazası“ idealine ulaşmak ve üretim yaparken yaşadığımız çevreyi korumak, kirlenmeyi önlemek ve iyileştirmek için;
        <br />
           <br />

*İş Sağlığı Güvenliği ve Çevre Koruma ile ilgili uygunluk yükümlülüklerine uyan;    <br />
*Faaliyetleri sonucunda oluşan olası tüm riskleri belirleyen ve bu riskleri kabul edilebilir seviyeye indirmek üzere çalışma başlatan;    <br />
*Ortaya çıkan atıklarını en aza indirmeye çalışan, önlenmesi mümkün olamayan atıklarını doğaya zarar vermeyecek bir şekilde yok edilmesini sağlayan;    <br />
*Doğal kaynak tüketimini kabul edilebilir seviyeye indirmeyi;    <br />
*Kurduğu sistemi ve faaliyetleri sürekli denetleyen ve ilgili tarafların denetlemesine açık tutan;    <br />
*Sürekli iyileştirme prensiplerine dayalı bir yönetim sistemini benimser ve gerçekleştireceğini taahhüt eder.    <br />
 
            <br />
Toplum ve çevre ile bütünleşmiş bir anlayışa sahip olan Borusan Mannesmann, İSG ve çevre koruma politikasını web sitesi, bültenler ve duyuru panoları ile toplumun ulaşımına açık tutmaktadır.​
            <br />
Ayrıca İSG ve Çevre Politikamız, Borusan Mannesmann ile ilk kez çalışacak taşeron ve tedarikçilerimize de sözleşme aşamasında bir mektup eşliğinde duyurulur.
            <br />
Borusan Mannesmann adına çalışan taşeronların da kanunların öngördüğü İş Sağlığı ve Güvenliği hükümlerine ve Borusan Mannesmann İSGÇ yönetim ve ilkelerine uyması sağlanmaktadır.
            <br />
