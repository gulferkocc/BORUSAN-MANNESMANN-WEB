 <h1> BORUSAN MANNESMANN BORU SANAYİ VE TİCARET A.Ş.<br />
     KAR DAĞITIM POLİTİKASI</h1>
<p>Şirketimiz kâr dağıtım politikası, 23.01.2014 tarihli ve 28891 sayılı Resmi Gazete'de yayınlanarak yürürlüğe giren Sermaye Piyasası Kurulu'nun Seri: II No.: 19.1 sayılı Kâr Payı Tebliği esas alınarak hazırlanmıştır.
    <br />
    <br />
Geçmiş dönem kâr ve temettü performansı incelendiğinde ekonominin en sıkıntılı dönemlerinde dahi şirketimizin kârlı olduğu ve halka açıldığımız 1994 yılından bu yana net kârımızın %50'sini veya daha üstünü ortaklarımıza temettü olarak dağıttığımız görülmektedir. Bu anlayışın gelecek dönemlerde de Türk Ticaret Kanunu, Sermaye Piyasası mevzuatı ve Sermaye Piyasası Kurulu uygulama ve ilke kararları çerçevesinde devam ettirilmesi hedeflenmektedir. Şirketimizin 2014 ve ileriki yıllar için kâr dağıtım politikası, dağıtılabilir kârdan %50 veya daha fazla oranda kâr payı dağıtmak şeklindedir. Bununla birlikte dağıtım oranımız iç ve dış ekonomik koşullardaki gelişmeler ile şirketimizin yatırım fırsatları ve finansman olanaklarına göre her yıl yeniden gözden geçirilecektir.
     <br />
    <br />
Kâr dağıtımına ilişkin yönetim kurulu önerisi, şekli ve içeriği kurulca belirlenen kâr dağıtım tablosu ile birlikte kurulun özel durumların kamuya açıklanmasına ilişkin düzenlemeleri çerçevesinde kamuya duyurulur.
     <br />
    <br />
Kâr dağıtımına ilişkin hususlar, Esas Sözleşme'mizin 26.'ıncı maddesinde aşağıda şekilde belirtilmiştir.
     <br />
    <br />
Şirketin umumi masraflarıyla muhtelif amortisman gibi, şirketçe ödenmesi veya ayrılması zaruri olan meblağlar ile şirket tüzel kişiliği tarafından ödenmesi zorunlu vergiler hesap senesi sonunda tespit olunan gelirlerden düşüldükten sonra geriye kalan ve yıllık bilançoda görülen safi (net) kâr, varsa geçmiş yıl zararlarının düşülmesinden sonra sırası ile aşağıda gösterilen şekilde tevzi olunur.
     <br />
    <br />
Birinci Tertip Kanuni Yedek Akçe:<br />
<b>a)</b> % 5'i kanuni yedek akçeye ayrılır,<br />
    <br /><br />
Birinci Temettü:<br />
<b>b)</b> Kalandan Sermaye Piyasası Kurulu'nca saptanan oran ve miktarda birinci temettü ayrılır.
Birinci temettüye halel gelmemek şartıyla kalan net dağıtılabilir kâr'dan aşağıdaki kâr kalemleri ayrılır.<br />
<b>c)</b> %10 nispetinde intifa senedi sahiplerine pay ayrılır.<br />
<b>d)</b> Yönetim kurulu üyeleri ile şirket müdür ve memurlarına yukarıda<b> (a)</b> ila <b>(c)</b> bentlerinde yazılı kalemlerin ödenmesinden sonra kalanın en çok %5'e kadarı genel kurulun takdirine göre kazanç payı olarak tahsis edilebilir. Yönetim kurulu üyeleri için belirlenmiş olan kazanç payının üyeler arasındaki dağılımı genel kurul tarafından Sermaye Piyasası'na ilişkin mevzuat da gözetilerek belirlenir.<br />
<b>e)</b> Azami % 5'e kadarı şirketin kurduğu veya katıldığı vakıflara ya da bu gibi kişi ve/veya kurumlara ayrılabilir.<br />
    <br /><br />
İkinci Temettü:<br />
<b>f) </b>Safi kâr'dan a, b, c, d ve e bentlerinde belirtilen meblağlar düşüldükten sonra kalan kısmı, umumi heyet kısmen veya tamamen ortaklara hisseleri nispetinde ikinci temettü hissesi olarak dağıtılabileceği gibi, kısmen veya tamamen fevkalade yedek akçe olarak ayrılabilir.<br /><br />

İkinci Tertip Kanuni Yedek Akçe:<br />
<b>g) </b>Pay sahipleriyle kâr'a iştirak eden diğer kimselere dağıtılması kararlaştırılmış olan kısımdan ödenmiş sermayenin %5'i oranında kâr payı düşüldükten sonra bulunan tutarın onda biri Türk Ticaret Kanunu'nun 466. maddesinin 2. fıkrasının 3. bendi uyarınca ikinci tertip kanuni yedek akçe olarak ayrılır.<br />
<b>h) </b>Yasa hükmü ile ayrılması gereken yedek akçeler ayrılmadıkça, esas sözleşmede pay sahipleri için belirlenen birinci temettü nakden ve/veya hisse senedi biçiminde dağıtılmadıkça başka yedek akçe ayrılmasına, ertesi yıla kâr aktarılmasına ve temettü dağıtımında imtiyazlı pay sahiplerine, katılma, kurucu ve adi intifa senedi sahiplerine, yönetim kurulu üyeleri ile memur, müstahdem ve işçilere, çeşitli amaçlarla kurulmuş olan vakıflara ve bu gibi kişi ve/veya kurumlara kâr payı dağıtılmasına karar verilemez.<br /><br />

Sermaye Piyasası Kanunu hükümleri gereğince ve sermaye piyasası mevzuatındaki esaslara uyulmak kaydıyla temettü avansı dağıtılabilir. Dağıtılan avansın mahsubu da yine ilgili mevzuat hükümlerine göre yapılır.
    <br /><br />
Kâr payı dağıtılmasına karar verilmesi halinde, kâr payı hak kazananlara nakit olarak ödenecek olup, dağıtım zamanı yönetim kurulu tarafından en geç dağıtım kararı verilen genel kurul toplantısının yapıldığı hesap dönemi sonu olmak üzere belirlenir.</p>
